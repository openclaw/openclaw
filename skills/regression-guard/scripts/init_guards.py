def generate_method_for_setting(name: str, cfg: dict) -> str:
    """Generate a single guard test method from a setting config dict."""
    value = cfg.get("value")
    reason = cfg.get("reason", "no reason given")
    evidence = cfg.get("evidence", "no evidence recorded")
    module = cfg.get("module", "")
    cls = cfg.get("class", "")

    value_repr = repr(value)

    lines = [
        f'    def test_{name}_locked(self) -> None:',
        f'        """Guard: {name} must be {value_repr}.',
        f'',
        f'        Reason: {reason}',
        f'        Evidence: {evidence}',
        f'        DO NOT change without full benchmark + updating this file.',
        f'        """',
    ]

    if module and cls:
        lines += [
            f'        from {module} import {cls}',
            f'        obj = {cls}()',
            f'        assert obj.{name} == {value_repr}, (',
            f'            f"{{name!r}} is {{obj.{name}!r}}, expected {value_repr}. "',
            f'            "{reason}"',
            f'        )',
        ]
    elif module:
        lines += [
            f'        import {module} as _mod',
            f'        src = inspect.getsource(_mod)',
            f'        assert {value_repr!r} in src, (',
            f'            f"Could not find {name}={value_repr!r} in {module}. "',
            f'            "{reason}"',
            f'        )',
        ]
    else:
        lines += [
            f'        # TODO: add assertion for {name} = {value_repr}',
            f'        # Hint: import the relevant module and check the value.',
        ]

    return "\n".join(lines)


def generate_cache_tests() -> str:
    return textwrap.dedent("""\
        def test_no_stale_cache_keys(self) -> None:
            \"\"\"Placeholder: verify cache key includes all mode-differentiating params.

            A common regression: two requests with different modes (e.g. hybrid vs vision)
            return the same cached result because the mode was not part of the cache key.

            To guard this for your project:
            1. Find the make_key() call in your app/cache layer
            2. Confirm all request parameters that change the result are included
            3. Replace this placeholder with a real assertion
            \"\"\"
            # Example (FastAPI app):
            # import inspect, your_app_module
            # src = inspect.getsource(your_app_module)
            # line = next(l for l in src.splitlines() if 'make_key' in l)
            # assert 'mode' in line, "Cache key must include request mode"
            pass  # Replace with project-specific assertion
    """).rstrip()


def build_guard_file(baseline: dict) -> str:
    import datetime

    settings = baseline.get("critical_settings", {})
    if settings:
        methods = "\n\n".join(
            generate_method_for_setting(name, cfg)
            for name, cfg in settings.items()
        )
    else:
        methods = "    pass  # Add guard tests here after running first benchmark"

    cache_body = generate_cache_tests()
    # indent cache_body
    cache_tests = "\n".join("    " + line for line in cache_body.splitlines())

    baseline_json = json.dumps(baseline, indent=4, ensure_ascii=False)
    # indent for Python source
    baseline_json_indented = "\n".join(
        ("    " if i > 0 else "") + line
        for i, line in enumerate(baseline_json.splitlines())
    )

    return GUARD_TEMPLATE.format(
        generated_date=datetime.date.today().isoformat(),
        baseline_file=baseline.get("benchmark_date", "unknown"),
        baseline_json=baseline_json_indented,
        test_methods=methods,
        cache_tests=cache_tests,
    )


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--project-dir", default=".", help="Project root directory")
    parser.add_argument("--benchmark-json", help="Path to baseline.json")
    parser.add_argument("--output", help="Output file (default: tests/test_regression_guard.py)")
    parser.add_argument("--example", action="store_true", help="Generate example file only")
    args = parser.parse_args()

    project_dir = Path(args.project_dir).resolve()

    if args.example:
        baseline = EXAMPLE_BASELINE
    elif args.benchmark_json:
        with open(args.benchmark_json, encoding="utf-8") as f:
            baseline = json.load(f)
    else:
        # Scan for existing benchmark docs
        docs_dir = project_dir / "docs"
        benchmark_files = list(docs_dir.glob("*Benchmark*")) + list(docs_dir.glob("*Sprint*"))
        if benchmark_files:
            print(f"Found {len(benchmark_files)} benchmark doc(s) in {docs_dir}:")
            for f in benchmark_files[:5]:
                print(f"  {f.name}")
            print()

        # Use empty baseline for scaffold
        baseline = {
            "project": project_dir.name,
            "benchmark_date": "TODO",
            "test_image": "data/samples/TODO.png",
            "metrics": {"symbols": 0, "connections": 0, "processing_time_s": 0},
            "critical_settings": {},
        }
        print("No --benchmark-json supplied. Generating scaffold with TODO placeholders.")
        print("Edit the generated file to add your actual baseline values.")

    output_path = Path(args.output) if args.output else project_dir / "tests" / "test_regression_guard.py"
    output_path.parent.mkdir(parents=True, exist_ok=True)

    content = build_guard_file(baseline)
    output_path.write_text(content, encoding="utf-8")
    print(f"[OK] Generated: {output_path}")
    print(f"     Baseline:  {baseline.get('benchmark_date', 'unknown')}")
    print(f"     Settings:  {len(baseline.get('critical_settings', {}))} guards")


if __name__ == "__main__":
    main()
