/**
 * generate-compositions — Reads all spec JSON files and generates
 * a Compositions.generated.tsx that auto-registers every spec as
 * a Remotion <Composition />.
 *
 * STRATEGY: Static imports (so Webpack can resolve them) but DEFERRED
 * parsing — parseMotionSpec(adaptRichSpec(raw)) runs at render time
 * inside React.useMemo, not at module evaluation time. This way:
 * - Webpack sees fixed import paths (no dynamic require)
 * - JSON is only parsed when a composition is selected in Studio
 * - Studio boot stays fast even with 40+ specs
 *
 * Usage:
 *   npx tsx tools/generate-compositions.ts
 *   (or: npm run gen)
 */
import fs from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const SPECS_DIR = path.join(
  projectRoot,
  "../../data/datasets/cutmv/motion/specs",
);
const OUT_FILE = path.join(projectRoot, "src/Compositions.generated.tsx");

const FPS = 30;
const WIDTH = 1080;
const HEIGHT = 1920;

function safeIdFromFilename(file: string): string {
  return file
    .replace(/\.json$/i, "")
    .replace(/_/g, "-")
    .toLowerCase();
}

/** Convert filename to a safe JS variable name: cutmv_premium_v001.json -> raw_v001 */
function varNameFromFilename(file: string): string {
  const match = file.match(/v(\d+)/i);
  return match
    ? "raw_v" + match[1]
    : "raw_" + file.replace(/[^a-zA-Z0-9]/g, "_");
}

function main() {
  if (!fs.existsSync(SPECS_DIR)) {
    console.error("Specs dir not found:", SPECS_DIR);
    process.exit(1);
  }

  const files = fs
    .readdirSync(SPECS_DIR)
    .filter((f) => f.toLowerCase().endsWith(".json"))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

  if (files.length === 0) {
    console.error("No JSON specs found in:", SPECS_DIR);
    process.exit(1);
  }

  // Build spec metadata + import info
  const items = files.map((file) => {
    const abs = path.join(SPECS_DIR, file);
    const raw = JSON.parse(fs.readFileSync(abs, "utf-8"));
    const id = safeIdFromFilename(file);
    const varName = varNameFromFilename(file);
    const durationInFrames: number =
      raw.format?.durationInFrames ?? raw.durationInFrames ?? 300;
    const importPath = "../../../data/datasets/cutmv/motion/specs/" + file;
    return { id, varName, durationInFrames, importPath, file };
  });

  // Build output parts using string concatenation (avoids template literal escaping issues)
  const lines: string[] = [];

  lines.push("/* AUTO-GENERATED — DO NOT EDIT MANUALLY.");
  lines.push(" * Generated by: tools/generate-compositions.ts");
  lines.push(" * Specs: " + items.length + " files from data/datasets/cutmv/motion/specs/");
  lines.push(" *");
  lines.push(" * DEFERRED PARSING: Each spec JSON is statically imported (Webpack-safe)");
  lines.push(" * but parseMotionSpec + adaptRichSpec run only at render time via useMemo.");
  lines.push(" * This keeps Studio boot fast with " + items.length + " specs.");
  lines.push(" */");
  lines.push('import React from "react";');
  lines.push('import { Composition } from "remotion";');
  lines.push('import { CutmvPremiumAdEngine } from "./compositions/CutmvPremiumAdEngine";');
  lines.push('import { parseMotionSpec } from "./engine/parser/parseMotionSpec";');
  lines.push('import { adaptRichSpec } from "./engine/parser/adaptRichSpec";');
  lines.push('import type { MotionSpec } from "./engine/parser/MotionSpecTypes";');
  lines.push("");
  lines.push("// ── Static JSON imports (Webpack resolves these at build time) ──");

  // Static import for each spec
  for (const it of items) {
    lines.push("import " + it.varName + " from " + JSON.stringify(it.importPath) + ";");
  }

  lines.push("");
  lines.push("/**");
  lines.push(" * DeferredEngine — Wraps CutmvPremiumAdEngine with deferred spec parsing.");
  lines.push(" * The raw JSON is passed as a prop; parsing + validation happens in useMemo");
  lines.push(" * only when this specific composition is rendered (selected in Studio).");
  lines.push(" */");
  lines.push("const DeferredEngine: React.FC<{ rawSpec: Record<string, unknown> }> = ({ rawSpec }) => {");
  lines.push("  const spec = React.useMemo<MotionSpec>(() => {");
  lines.push("    return parseMotionSpec(adaptRichSpec(rawSpec) as Record<string, unknown>);");
  lines.push("  }, [rawSpec]);");
  lines.push("");
  lines.push("  return <CutmvPremiumAdEngine spec={spec} />;");
  lines.push("};");
  lines.push("");
  lines.push("export const GeneratedCompositions: React.FC = () => (");
  lines.push("  <>");

  // Composition registrations
  for (const it of items) {
    lines.push("    <Composition");
    lines.push('      key="' + it.id + '"');
    lines.push('      id="' + it.id + '"');
    lines.push("      component={DeferredEngine}");
    lines.push("      width={" + WIDTH + "}");
    lines.push("      height={" + HEIGHT + "}");
    lines.push("      fps={" + FPS + "}");
    lines.push("      durationInFrames={" + it.durationInFrames + "}");
    lines.push("      defaultProps={{ rawSpec: " + it.varName + " as Record<string, unknown> }}");
    lines.push("    />");
  }

  lines.push("  </>");
  lines.push(");");
  lines.push("");

  const content = lines.join("\n");

  fs.writeFileSync(OUT_FILE, content, "utf-8");
  console.log("Wrote " + items.length + " compositions (deferred parsing) -> " + OUT_FILE);
}

main();
