export type ProjectUiTextKey = keyof typeof PROJECT_UI_EN;

export function projectUiText(key: ProjectUiTextKey, params?: Record<string, string>): string {
  const value = i18n.getLocale() === "es" ? PROJECT_UI_ES[key] : PROJECT_UI_EN[key];
  if (!params) {
    return value;
  }
  return value.replace(/\{(\w+)\}/g, (_, paramKey: string) => params[paramKey] ?? `{${paramKey}}`);
}
