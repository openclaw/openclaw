#!/usr/bin/env bash
# docker/vps-setup.sh
# First-time setup for OpenClaw on Hostinger VPS (Ubuntu 24.04 LTS).
#
# Run ONCE as root directly on the VPS:
#   ssh root@srv1414058.hstgr.cloud 'bash -s' < docker/vps-setup.sh
#
# What this script does:
#   1. Updates system packages
#   2. Installs Docker Engine (official GPG repo)
#   3. Installs Nginx + Certbot
#   4. Creates /opt/openclaw (app home) with skeleton files
#   5. Deploys the Nginx site configuration
#   6. Obtains Let's Encrypt TLS certificate for openclaw.yahwan.biz
#   7. Prints next steps
#
# SECURITY: This script does NOT handle secrets.
#           Fill in /opt/openclaw/.env AFTER running this script.
#
# Idempotent: safe to re-run; skips already-present packages/files.

set -euo pipefail

# ── Variables ────────────────────────────────────────────────────────────────
DOMAIN="openclaw.yahwan.biz"
APP_DIR="/opt/openclaw"
NGINX_SITE="/etc/nginx/sites-available/openclaw"
NGINX_ENABLED="/etc/nginx/sites-enabled/openclaw"
CERTBOT_WEBROOT="/var/www/certbot"
CERTBOT_EMAIL="${CERTBOT_EMAIL:-}"   # Set via env or prompted below

# Script must run as root
if [[ "$(id -u)" -ne 0 ]]; then
  echo "ERROR: Run as root (sudo -i or direct SSH as root)"
  exit 1
fi

echo "=== [1/7] Updating system packages ==="
apt-get update -q
apt-get upgrade -y -q --no-install-recommends

echo "=== [2/7] Installing Docker Engine ==="
if ! command -v docker &>/dev/null; then
  apt-get install -y -q ca-certificates curl gnupg
  install -m 0755 -d /etc/apt/keyrings
  curl -fsSL https://download.docker.com/linux/ubuntu/gpg \
    | gpg --dearmor -o /etc/apt/keyrings/docker.gpg
  chmod a+r /etc/apt/keyrings/docker.gpg
  echo \
    "deb [arch=$(dpkg --print-architecture) signed-by=/etc/apt/keyrings/docker.gpg] \
     https://download.docker.com/linux/ubuntu $(. /etc/os-release && echo "$VERSION_CODENAME") stable" \
    | tee /etc/apt/sources.list.d/docker.list > /dev/null
  apt-get update -q
  apt-get install -y -q docker-ce docker-ce-cli containerd.io docker-buildx-plugin docker-compose-plugin
  systemctl enable --now docker
  echo "Docker installed: $(docker --version)"
else
  echo "Docker already installed: $(docker --version)"
fi

echo "=== [3/7] Installing Nginx + Certbot ==="
apt-get install -y -q nginx certbot python3-certbot-nginx
systemctl enable nginx

echo "=== [4/7] Creating application directory: $APP_DIR ==="
mkdir -p "$APP_DIR"
mkdir -p "$CERTBOT_WEBROOT"

# Write docker-compose.prod.yml to the app dir (inline, from the repo copy)
if [[ ! -f "$APP_DIR/docker-compose.prod.yml" ]]; then
  cat > "$APP_DIR/docker-compose.prod.yml" << 'COMPOSE_EOF'
# Managed by docker/vps-setup.sh — update via docker/deploy.sh
# Do NOT edit manually unless you know what you're doing.
# Source: docker/docker-compose.prod.yml in the openclaw repo.

services:
  openclaw-gateway:
    image: ${DOCKER_IMAGE:-piboonsak/openclaw:latest}
    container_name: openclaw-sgnl-openclaw-1
    user: "1000:1000"
    read_only: true
    security_opt:
      - no-new-privileges:true
    cap_drop:
      - ALL
    pids_limit: 200
    ports:
      - "127.0.0.1:${OPENCLAW_GATEWAY_PORT:-18789}:18789"
    environment:
      OPENCLAW_GATEWAY_TOKEN: ${OPENCLAW_GATEWAY_TOKEN}
      ANTHROPIC_API_KEY:      ${ANTHROPIC_API_KEY:-}
      MOONSHOT_API_KEY:       ${MOONSHOT_API_KEY:-}
      OPENAI_API_KEY:         ${OPENAI_API_KEY:-}
      GEMINI_API_KEY:         ${GEMINI_API_KEY:-}
      NODE_ENV:               production
      HOME:                   /home/node
      TERM:                   xterm-256color
      OPENCLAW_LOAD_SHELL_ENV: "0"
      BRAVE_API_KEY:          ${BRAVE_API_KEY:-}
      FIRECRAWL_API_KEY:      ${FIRECRAWL_API_KEY:-}
      ELEVENLABS_API_KEY:     ${ELEVENLABS_API_KEY:-}
    volumes:
      - openclaw-state:/data/openclaw/state
      - openclaw-workspace:/data/openclaw/workspace
      - type: tmpfs
        target: /tmp
        tmpfs:
          size: 100M
          mode: 1777
    deploy:
      resources:
        limits:
          cpus: '2'
          memory: 2G
        reservations:
          cpus: '0.5'
          memory: 512M
    restart: unless-stopped
    healthcheck:
      test: ["CMD", "curl", "-f", "http://localhost:18789/health"]
      interval: 30s
      timeout: 10s
      start_period: 40s
      retries: 3
    init: true
    networks:
      - openclaw-net

volumes:
  openclaw-state:
    driver: local
  openclaw-workspace:
    driver: local

networks:
  openclaw-net:
    driver: bridge
COMPOSE_EOF
  echo "Wrote $APP_DIR/docker-compose.prod.yml"
fi

# Write .env template if not present
if [[ ! -f "$APP_DIR/.env" ]]; then
  cat > "$APP_DIR/.env" << 'ENV_EOF'
# /opt/openclaw/.env  — FILL IN SECRETS, never commit this file
# Generated by docker/vps-setup.sh

DOCKER_IMAGE=piboonsak/openclaw:latest
OPENCLAW_GATEWAY_PORT=18789

# REQUIRED: generate with: openssl rand -hex 24
OPENCLAW_GATEWAY_TOKEN=CHANGE_ME

# At least one AI provider key required
ANTHROPIC_API_KEY=
MOONSHOT_API_KEY=
OPENAI_API_KEY=
GEMINI_API_KEY=

# Optional tool keys
BRAVE_API_KEY=
FIRECRAWL_API_KEY=
ELEVENLABS_API_KEY=
ENV_EOF
  chmod 600 "$APP_DIR/.env"
  echo "Template written to $APP_DIR/.env  <-- EDIT THIS FILE before starting"
fi

echo "=== [5/7] Deploying Nginx site configuration ==="
# Use a pre-SSL config first (no ssl_certificate lines) so Certbot can run
cat > "$NGINX_SITE" << 'NGINX_PRE_EOF'
# Temporary HTTP-only config for Let's Encrypt challenge
# Certbot will replace/amend this file during certificate issuance.
server {
    listen 80;
    listen [::]:80;
    server_name openclaw.yahwan.biz;

    location /.well-known/acme-challenge/ {
        root /var/www/certbot;
    }

    location / {
        return 301 https://$host$request_uri;
    }
}
NGINX_PRE_EOF

ln -sf "$NGINX_SITE" "$NGINX_ENABLED"
rm -f /etc/nginx/sites-enabled/default

nginx -t
systemctl reload nginx
echo "Nginx pre-SSL config active"

echo "=== [6/7] Obtaining Let's Encrypt TLS certificate ==="
if [[ -z "$CERTBOT_EMAIL" ]]; then
  read -rp "Enter email for Let's Encrypt notifications (e.g. you@example.com): " CERTBOT_EMAIL
fi

if [[ -f "/etc/letsencrypt/live/$DOMAIN/fullchain.pem" ]]; then
  echo "Certificate for $DOMAIN already exists, skipping Certbot run."
else
  certbot --nginx \
    -d "$DOMAIN" \
    --non-interactive \
    --agree-tos \
    --email "$CERTBOT_EMAIL" \
    --redirect
fi

# Now copy the production Nginx config (full SSL config from repo)
if [[ -f "$APP_DIR/nginx-openclaw.conf" ]]; then
  cp "$APP_DIR/nginx-openclaw.conf" "$NGINX_SITE"
  nginx -t && systemctl reload nginx
  echo "Production Nginx config applied"
fi

echo "=== [7/7] Enabling Certbot auto-renewal ==="
systemctl enable --now certbot.timer 2>/dev/null || \
  (echo "certbot.timer not found, using cron fallback" && \
   (crontab -l 2>/dev/null; echo "0 3 * * * certbot renew --quiet && systemctl reload nginx") | crontab -)

# ── Summary ───────────────────────────────────────────────────────────────────
echo ""
echo "╔══════════════════════════════════════════════════════════════╗"
echo "║              VPS setup complete — NEXT STEPS                ║"
echo "╠══════════════════════════════════════════════════════════════╣"
echo "║ 1. Edit secrets:  nano $APP_DIR/.env"
echo "║    Set OPENCLAW_GATEWAY_TOKEN and at least one API key."
echo "║"
echo "║ 2. Start container:"
echo "║    cd $APP_DIR"
echo "║    docker compose -f docker-compose.prod.yml up -d"
echo "║"
echo "║ 3. Verify:"
echo "║    curl -s https://$DOMAIN/health"
echo "║    docker logs openclaw-sgnl-openclaw-1 -f"
echo "║"
echo "║ 4. (Optional) Add GitHub Secret VPS_SSH_KEY for CI/CD auto-deploy"
echo "╚══════════════════════════════════════════════════════════════╝"
