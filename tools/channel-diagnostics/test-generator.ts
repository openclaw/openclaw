async function main() {
  const args = process.argv.slice(2);
  const channelId = args.find((arg) => !arg.startsWith("--"))?.replace("--channel=", "");

  if (!channelId) {
    console.error("❌ Error: Channel ID required");
    console.log("\nUsage:");
    console.log(
      "  node --import tsx tools/channel-diagnostics/test-generator.ts --channel <channel-id>",
    );
    console.log("\nExample:");
    console.log(
      "  node --import tsx tools/channel-diagnostics/test-generator.ts --channel telegram",
    );
    process.exit(1);
  }

  console.log(`🔧 Generating test suite for ${channelId}...\n`);

  // Check if channel exists
  const channelPath = join(process.cwd(), "extensions", channelId);
  if (!existsSync(channelPath)) {
    console.error(`❌ Error: Channel '${channelId}' not found in extensions/`);
    process.exit(1);
  }

  // Generate test suite
  const testSuite = generateTestSuite(channelId);
  const testPlan = generateTestPlan(channelId);

  // Create output directory
  const outputDir = join(process.cwd(), "tools", "channel-diagnostics", "generated");
  await mkdir(outputDir, { recursive: true });

  // Write files
  const testFilePath = join(outputDir, `${channelId}.test.ts`);
  const planFilePath = join(outputDir, `${channelId}-test-plan.md`);

  await writeFile(testFilePath, testSuite);
  await writeFile(planFilePath, testPlan);

  console.log("✅ Test suite generated successfully!\n");
  console.log("📄 Files created:");
  console.log(`   • ${testFilePath}`);
  console.log(`   • ${planFilePath}`);
  console.log("\n💡 Next steps:");
  console.log(`   1. Review the generated test file`);
  console.log(`   2. Implement the TODO test cases`);
  console.log(`   3. Move the test file to extensions/${channelId}/src/`);
  console.log(`   4. Run: pnpm test:extension ${channelId}`);
  console.log();
}

main().catch((error) => {
  console.error("❌ Fatal error:", error);
  process.exit(1);
});
