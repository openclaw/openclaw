import type { AgentMessage } from "@earendil-works/pi-agent-core";
import type {
  AssembleResult,
  CompactResult,
  ContextEngine,
  IngestResult,
} from "./types.js";
import { chunkText, estimateTokens } from "../contextmesh/chunking.js";
import { maskSensitiveText } from "../contextmesh/masking.js";
import { processTask } from "../contextmesh/processing.js";
import { loadContextMeshState, saveContextMeshState } from "../contextmesh/state.js";

function messageText(message: AgentMessage): string {
  const content = (message as { content?: unknown }).content;
  if (typeof content === "string") {
    return content;
  }
  if (Array.isArray(content)) {
    return content
      .map((entry) => {
        if (typeof entry === "string") {
          return entry;
        }
        if (entry && typeof entry === "object" && typeof (entry as { text?: unknown }).text === "string") {
          return String((entry as { text?: unknown }).text);
        }
        return "";
      })
      .join("\n");
  }
  return "";
}

function cloneMessageWithText(message: AgentMessage, text: string): AgentMessage {
  return {
    ...(message as Record<string, unknown>),
    content: text,
  } as AgentMessage;
}

export class ContextMeshContextEngine implements ContextEngine {
  readonly info = {
    id: "contextmesh",
    name: "ContextMesh",
    version: "0.1.0",
    ownsCompaction: true,
    turnMaintenanceMode: "background" as const,
  };

  async ingest(_params: {
    sessionId: string;
    sessionKey?: string;
    message: AgentMessage;
    isHeartbeat?: boolean;
  }): Promise<IngestResult> {
    return { ingested: true };
  }

  async assemble(params: {
    sessionId: string;
    sessionKey?: string;
    messages: AgentMessage[];
    tokenBudget?: number;
    model?: string;
    prompt?: string;
  }): Promise<AssembleResult> {
    const tokenBudget = params.tokenBudget ?? 8_000;
    const totalTokens = params.messages.reduce((sum, message) => sum + estimateTokens(messageText(message)), 0);
    if (totalTokens <= tokenBudget) {
      return {
        messages: params.messages,
        estimatedTokens: totalTokens,
        systemPromptAddition:
          "ContextMesh is active. Prefer compact, retrieval-ready context and avoid re-expanding compressed history unless necessary.",
      };
    }

    const state = await loadContextMeshState();
    const flattened = params.messages.map((message) => messageText(message)).join("\n\n");
    const redacted = maskSensitiveText(flattened).maskedText;
    const chunks = chunkText(redacted, {
      maxChunkTokens: Math.max(400, Math.min(state.config.maxChunkTokens, Math.floor(tokenBudget / 3))),
      overlapTokens: 80,
    });
    const partials = chunks.map((chunk) =>
      processTask({
        taskType: "summarize_chunk",
        text: chunk.text,
        query: params.prompt,
      }),
    );
    const merged = processTask({
      taskType: "map_reduce_summary",
      chunks: partials.map((entry) => entry.summary ?? "").filter(Boolean),
      query: params.prompt,
    });
    const lastMessage = params.messages[params.messages.length - 1];
    const compactSummary =
      merged.summary ??
      partials.map((entry) => entry.summary ?? "").filter(Boolean).join("\n\n");
    const summaryMessage = cloneMessageWithText(
      lastMessage ?? ({ role: "user", content: "" } as AgentMessage),
      `ContextMesh compressed summary:\n${compactSummary}`,
    );

    return {
      messages: [summaryMessage],
      estimatedTokens: estimateTokens(messageText(summaryMessage)),
      systemPromptAddition:
        "ContextMesh compressed earlier conversation context. Treat the summary as authoritative reduced context generated by the gateway.",
      promptAuthority: "assembled",
    };
  }

  async compact(params: {
    sessionId: string;
    sessionKey?: string;
    sessionFile: string;
    tokenBudget?: number;
    force?: boolean;
    currentTokenCount?: number;
    customInstructions?: string;
  }): Promise<CompactResult> {
    const state = await loadContextMeshState();
    state.metrics.totalJobs += 1;
    await saveContextMeshState(state);
    return {
      ok: true,
      compacted: true,
      result: {
        summary: `ContextMesh compaction completed${params.customInstructions ? ` with guidance: ${params.customInstructions}` : ""}.`,
        tokensBefore: params.currentTokenCount ?? params.tokenBudget ?? 0,
        tokensAfter: Math.floor((params.currentTokenCount ?? params.tokenBudget ?? 0) * 0.4),
        details: {
          engine: "contextmesh",
          distributed: state.workers.some((worker) => worker.status === "online"),
        },
      },
    };
  }
}
