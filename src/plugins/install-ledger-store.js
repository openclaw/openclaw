import path from "node:path";
import { z } from "zod";
import { resolveStateDir } from "../config/paths.js";
import { readJsonFile, readJsonFileSync, writeJsonAtomic } from "../infra/json-files.js";
import { safeParseWithSchema } from "../utils/zod-parse.js";
import { recordPluginInstall } from "./installs.js";
export const PLUGIN_INSTALL_LEDGER_VERSION = 1;
export const PLUGIN_INSTALL_LEDGER_STORE_PATH = path.join("plugins", "installs.json");
export const PLUGIN_INSTALL_LEDGER_WARNING = "DO NOT EDIT. This file is generated by OpenClaw plugin install/update/uninstall commands. Use `openclaw plugins install/update/uninstall` instead.";
export const PLUGIN_INSTALLS_CONFIG_PATH = ["plugins", "installs"];
const PluginInstallRecordSchema = z
    .object({
    source: z.string(),
})
    .passthrough();
const PluginInstallLedgerSchema = z
    .object({
    version: z.literal(PLUGIN_INSTALL_LEDGER_VERSION),
    warning: z.string().optional(),
    updatedAtMs: z.number(),
    records: z.record(z.string(), PluginInstallRecordSchema),
})
    .passthrough();
function parsePluginInstallLedger(value) {
    return safeParseWithSchema(PluginInstallLedgerSchema, value);
}
function cloneInstallRecords(records) {
    return structuredClone(records ?? {});
}
export function resolvePluginInstallLedgerStorePath(options = {}) {
    if (options.filePath) {
        return options.filePath;
    }
    const env = options.env ?? process.env;
    const stateDir = options.stateDir ?? resolveStateDir(env);
    return path.join(stateDir, PLUGIN_INSTALL_LEDGER_STORE_PATH);
}
export async function readPersistedPluginInstallLedger(options = {}) {
    const parsed = await readJsonFile(resolvePluginInstallLedgerStorePath(options));
    return parsePluginInstallLedger(parsed);
}
export function readPersistedPluginInstallLedgerSync(options = {}) {
    const parsed = readJsonFileSync(resolvePluginInstallLedgerStorePath(options));
    return parsePluginInstallLedger(parsed);
}
export async function writePersistedPluginInstallLedger(records, options = {}) {
    const filePath = resolvePluginInstallLedgerStorePath(options);
    await writeJsonAtomic(filePath, {
        version: PLUGIN_INSTALL_LEDGER_VERSION,
        warning: PLUGIN_INSTALL_LEDGER_WARNING,
        updatedAtMs: (options.now ?? (() => new Date()))().getTime(),
        records,
    }, {
        trailingNewline: true,
        ensureDirMode: 0o700,
        mode: 0o600,
    });
    return filePath;
}
export async function loadPluginInstallRecords(params = {}) {
    const ledger = await readPersistedPluginInstallLedger(params);
    if (ledger) {
        return cloneInstallRecords(ledger.records);
    }
    return cloneInstallRecords(params.config?.plugins?.installs);
}
export function loadPluginInstallRecordsSync(params = {}) {
    const ledger = readPersistedPluginInstallLedgerSync(params);
    if (ledger) {
        return cloneInstallRecords(ledger.records);
    }
    return cloneInstallRecords(params.config?.plugins?.installs);
}
export function withPluginInstallRecords(config, records) {
    return {
        ...config,
        plugins: {
            ...config.plugins,
            installs: records,
        },
    };
}
export function withoutPluginInstallRecords(config) {
    if (!config.plugins?.installs) {
        return config;
    }
    const { installs: _installs, ...plugins } = config.plugins;
    if (Object.keys(plugins).length === 0) {
        const { plugins: _plugins, ...rest } = config;
        return rest;
    }
    return {
        ...config,
        plugins,
    };
}
export function recordPluginInstallInRecords(records, update) {
    return recordPluginInstall({ plugins: { installs: records } }, update).plugins?.installs ?? {};
}
export function removePluginInstallRecordFromRecords(records, pluginId) {
    const { [pluginId]: _removed, ...rest } = records;
    return rest;
}
