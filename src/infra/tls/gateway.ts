// Gateway TLS runtime loads configured certificates or generates a local
// self-signed pair, returning server-ready options plus client fingerprint.
import { execFile } from "node:child_process";
import { X509Certificate } from "node:crypto";
import fs from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import tls from "node:tls";
import { promisify } from "node:util";
import type { GatewayTlsConfig } from "../../config/types.gateway.js";
import { CONFIG_DIR, ensureDir, resolveUserPath, shortenHomeInString } from "../../utils.js";
import { pathExists } from "../fs-safe.js";
import { resolveSystemBin } from "../resolve-system-bin.js";
import { normalizeFingerprint } from "./fingerprint.js";

const execFileAsync = promisify(execFile);

// Gateway TLS runtime carries loaded cert material plus the normalized SHA-256
// fingerprint advertised to clients.
export type GatewayTlsRuntime = {
  enabled: boolean;
  required: boolean;
  certPath?: string;
  keyPath?: string;
  caPath?: string;
  fingerprintSha256?: string;
  tlsOptions?: tls.TlsOptions;
  error?: string;
};

async function generateSelfSignedCert(params: {
  certPath: string;
  keyPath: string;
  log?: { info?: (msg: string) => void };
}): Promise<void> {
  const certDir = path.dirname(params.certPath);
  const keyDir = path.dirname(params.keyPath);
  await ensureDir(certDir);
  if (keyDir !== certDir) {
    await ensureDir(keyDir);
  }
  const opensslBin = resolveSystemBin("openssl");
  if (!opensslBin) {
    throw new Error(
      "openssl not found in trusted system directories. Install it in an OS-managed location.",
    );
  }
  // Use execFile with a trusted system binary; certificate paths are arguments,
  // not shell text.
  await execFileAsync(opensslBin, [
    "req",
    "-x509",
    "-newkey",
    "rsa:2048",
    "-sha256",
    "-days",
    "3650",
    "-nodes",
    "-keyout",
    params.keyPath,
    "-out",
    params.certPath,
    "-subj",
    "/CN=openclaw-gateway",
    "-addext",
    `subjectAltName=DNS:localhost,DNS:${os.hostname()},IP:127.0.0.1,IP:::1`,
  ]);
  await fs.chmod(params.keyPath, 0o600).catch(() => {});
  await fs.chmod(params.certPath, 0o600).catch(() => {});
  params.log?.info?.(
    `gateway tls: generated self-signed cert at ${shortenHomeInString(params.certPath)}`,
  );
}

/** Build a successful GatewayTlsRuntime result from loaded PEM material. */
function buildTlsResult(
  certPath: string,
  keyPath: string,
  caPath: string | undefined,
  certPem: string,
  keyPem: string,
  caPem: string | undefined,
): { ok: GatewayTlsRuntime } | { error: string } {
  try {
    const x509 = new X509Certificate(certPem);
    const fingerprintSha256 = normalizeFingerprint(x509.fingerprint256 ?? "");
    if (!fingerprintSha256) {
      return {
        error: "gateway tls: unable to compute certificate fingerprint",
      };
    }
    return {
      ok: {
        enabled: true,
        required: true,
        certPath,
        keyPath,
        caPath,
        fingerprintSha256,
        tlsOptions: {
          cert: certPem,
          key: keyPem,
          ca: caPem,
          minVersion: "TLSv1.3",
        },
      },
    };
  } catch {
    return { error: "gateway tls: failed to load cert" };
  }
}

/** Returns true when the PEM certificate was auto-generated by an older
 * OpenClaw version (CN=openclaw-gateway) without a subjectAltName extension.
 * These certificates need to be regenerated with SANs for strict TLS clients. */
function isOpenClawGeneratedCnOnlyCert(certPem: string): boolean {
  try {
    const x509 = new X509Certificate(certPem);
    return x509.subject.includes("CN=openclaw-gateway") && !x509.subjectAltName;
  } catch {
    return false;
  }
}

/** Check whether an existing cert/key pair was generated by an older OpenClaw
 * version and needs regeneration with subjectAltName.  Returns true when the
 * cert was regenerated, false when nothing was done.
 *
 * Regeneration is transactional: the old files are renamed to backup paths
 * before generating new ones, and restored if generation fails. */
async function maybeUpgradeCnOnlyCert(params: {
  certPath: string;
  keyPath: string;
  log?: { info?: (msg: string) => void; warn?: (msg: string) => void };
}): Promise<boolean> {
  const certPem = await fs.readFile(params.certPath, "utf8").catch(() => null);
  if (!certPem || !isOpenClawGeneratedCnOnlyCert(certPem)) {
    return false;
  }
  const certBak = params.certPath + ".bak";
  const keyBak = params.keyPath + ".bak";
  params.log?.warn?.("gateway tls: upgrading CN-only certificate to include subjectAltName");
  try {
    // Rename originals to backup before generating fresh ones.
    await fs.rename(params.certPath, certBak);
    await fs.rename(params.keyPath, keyBak);
    await generateSelfSignedCert({
      certPath: params.certPath,
      keyPath: params.keyPath,
      log: params.log,
    });
    // New cert generated successfully — remove backups.
    await fs.unlink(certBak).catch(() => {});
    await fs.unlink(keyBak).catch(() => {});
    return true;
  } catch (err) {
    // Generation failed — restore originals from backup.
    params.log?.warn?.("gateway tls: CN-only upgrade failed, restoring original certificate");
    await fs.rename(certBak, params.certPath).catch(() => {});
    await fs.rename(keyBak, params.keyPath).catch(() => {});
    // Re-throw so the caller sees the failure.
    throw err;
  }
}

/** Load or generate gateway TLS material and return server-ready TLS options. */
export async function loadGatewayTlsRuntime(
  cfg: GatewayTlsConfig | undefined,
  log?: { info?: (msg: string) => void; warn?: (msg: string) => void },
): Promise<GatewayTlsRuntime> {
  if (!cfg || cfg.enabled !== true) {
    return { enabled: false, required: false };
  }

  const autoGenerate = cfg.autoGenerate !== false;
  const baseDir = path.join(CONFIG_DIR, "gateway", "tls");
  // Only blank/whitespace values fall back to the default. Any non-empty path is
  // passed through verbatim so resolveUserPath owns all normalization (it trims
  // and expands ~); trimming here would duplicate it and silently rewrite paths
  // that contain leading/trailing spaces.
  const certPath = resolveUserPath(
    typeof cfg.certPath === "string" && cfg.certPath.trim()
      ? cfg.certPath
      : path.join(baseDir, "gateway-cert.pem"),
  );
  const keyPath = resolveUserPath(
    typeof cfg.keyPath === "string" && cfg.keyPath.trim()
      ? cfg.keyPath
      : path.join(baseDir, "gateway-key.pem"),
  );
  const caPath = cfg.caPath ? resolveUserPath(cfg.caPath) : undefined;

  const hasCert = await pathExists(certPath);
  const hasKey = await pathExists(keyPath);

  if (!hasCert && !hasKey && autoGenerate) {
    try {
      await generateSelfSignedCert({ certPath, keyPath, log });
    } catch (err) {
      return {
        enabled: false,
        required: true,
        certPath,
        keyPath,
        error: `gateway tls: failed to generate cert (${String(err)})`,
      };
    }
  }

  if (!(await pathExists(certPath)) || !(await pathExists(keyPath))) {
    return {
      enabled: false,
      required: true,
      certPath,
      keyPath,
      error: "gateway tls: cert/key missing",
    };
  }

  try {
    const cert = await fs.readFile(certPath, "utf8");
    const key = await fs.readFile(keyPath, "utf8");
    const ca = caPath ? await fs.readFile(caPath, "utf8") : undefined;

    // Upgrade path: if the loaded cert is an older OpenClaw-generated CN-only
    // certificate (missing subjectAltName), regenerate with SANs.  Only run
    // for default cert/key paths — explicit user-provided paths are never
    // rewritten, even if the certificate happens to be CN=openclaw-gateway.
    const isDefaultCertPath = !(typeof cfg.certPath === "string" && cfg.certPath.trim());
    const isDefaultKeyPath = !(typeof cfg.keyPath === "string" && cfg.keyPath.trim());
    if (autoGenerate && caPath === undefined && isDefaultCertPath && isDefaultKeyPath) {
      const upgraded = await maybeUpgradeCnOnlyCert({
        certPath,
        keyPath,
        log,
      }).catch(() => false);
      if (upgraded) {
        // Re-read the freshly generated material
        const freshCert = await fs.readFile(certPath, "utf8");
        const freshKey = await fs.readFile(keyPath, "utf8");
        const builtUpgrade = buildTlsResult(
          certPath,
          keyPath,
          undefined,
          freshCert,
          freshKey,
          undefined,
        );
        if ("error" in builtUpgrade) {
          return {
            enabled: false,
            required: true,
            certPath,
            keyPath,
            error: builtUpgrade.error,
          };
        }
        return builtUpgrade.ok;
      }
    }

    const built = buildTlsResult(certPath, keyPath, caPath, cert, key, ca);
    if ("error" in built) {
      return {
        enabled: false,
        required: true,
        certPath,
        keyPath,
        caPath,
        error: built.error,
      };
    }
    return built.ok;
  } catch (err) {
    return {
      enabled: false,
      required: true,
      certPath,
      keyPath,
      caPath,
      error: `gateway tls: failed to load cert (${String(err)})`,
    };
  }
}
