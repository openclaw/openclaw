import fs from "node:fs/promises";
import path from "node:path";
import type { OpenClawConfig } from "../config/config.js";
import { logVerbose } from "../globals.js";
import { resolveAgentWorkspaceDir } from "./agent-scope.js";

const CLAUDE_MD_FILENAME = "CLAUDE.md";
const SOUL_MD_FILENAME = "SOUL.md";
const AGENTS_MD_FILENAME = "AGENTS.md";
const MAX_BRIDGE_FILE_BYTES = 256 * 1024; // 256KB safety cap per source file
const BRIDGE_MARKER = "<!-- openclaw-persona-bridge -->";

/**
 * Bridge fleet agent persona files (SOUL.md, AGENTS.md) into a CLAUDE.md file
 * in the ACP session's working directory so CLI backends pick up the agent's identity.
 *
 * If CLAUDE.md already exists:
 * - If it was generated by this bridge (contains marker), it is refreshed for the new agent.
 * - If it is a user/project file (no marker), it is left untouched.
 */
export async function bridgeAgentPersonaToClaudeMd(params: {
  cfg: OpenClawConfig;
  agentId: string;
  sessionCwd: string;
}): Promise<{ bridged: boolean; reason?: string }> {
  const { cfg, agentId, sessionCwd } = params;

  const claudeMdPath = path.join(sessionCwd, CLAUDE_MD_FILENAME);

  // Check existing CLAUDE.md — only skip if it's a user/project file (no bridge marker)
  try {
    const existing = await fs.readFile(claudeMdPath, "utf-8");
    if (!existing.includes(BRIDGE_MARKER)) {
      return { bridged: false, reason: "existing-claude-md" };
    }
    // Bridge-generated file exists — will refresh it for the new agent
  } catch {
    // File doesn't exist — proceed
  }

  const workspaceDir = resolveAgentWorkspaceDir(cfg, agentId);

  // Read persona source files
  const soulContent = await safeReadFile(path.join(workspaceDir, SOUL_MD_FILENAME));
  const agentsContent = await safeReadFile(path.join(workspaceDir, AGENTS_MD_FILENAME));

  if (!soulContent && !agentsContent) {
    // Clean up stale bridge file if it exists — prevents previous agent's persona
    // from leaking into a new agent that has no persona files.
    try {
      const existing = await fs.readFile(claudeMdPath, "utf-8").catch(() => null);
      if (existing?.includes(BRIDGE_MARKER)) {
        await fs.unlink(claudeMdPath);
        logVerbose(
          `acp-persona-bridge: removed stale bridge CLAUDE.md for agent "${agentId}" (no persona files)`,
        );
      }
    } catch {
      // Best effort cleanup
    }
    return { bridged: false, reason: "no-persona-files" };
  }

  // Synthesize CLAUDE.md from available persona files
  const sections: string[] = [
    BRIDGE_MARKER,
    `# Agent Persona Bridge`,
    ``,
    `> Auto-generated by OpenClaw ACP persona bridge for agent "${agentId}".`,
    `> Source: fleet agent workspace persona files (SOUL.md, AGENTS.md).`,
    ``,
  ];

  if (soulContent) {
    sections.push(`## Agent Identity (SOUL.md)`, ``, soulContent, ``);
  }

  if (agentsContent) {
    sections.push(`## Agent Workspace Rules (AGENTS.md)`, ``, agentsContent, ``);
  }

  const content = sections.join("\n");

  try {
    await fs.writeFile(claudeMdPath, content, { encoding: "utf-8" });
    logVerbose(
      `acp-persona-bridge: bridged persona for agent "${agentId}" to ${claudeMdPath}`,
    );
    return { bridged: true };
  } catch (err) {
    logVerbose(
      `acp-persona-bridge: failed to write CLAUDE.md for agent "${agentId}": ${String(err)}`,
    );
    return { bridged: false, reason: "write-error" };
  }
}

async function safeReadFile(filePath: string): Promise<string | null> {
  try {
    const stat = await fs.stat(filePath);
    if (stat.size > MAX_BRIDGE_FILE_BYTES) {
      logVerbose(
        `acp-persona-bridge: skipping ${filePath} — exceeds ${MAX_BRIDGE_FILE_BYTES} byte limit (${stat.size} bytes)`,
      );
      return null;
    }
    const content = await fs.readFile(filePath, "utf-8");
    return content.trim() || null;
  } catch {
    return null;
  }
}
