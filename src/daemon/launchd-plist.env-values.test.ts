// Generated service-env value parsing must self-heal corrupted quoting so
// doctor --fix re-stages converge instead of preserving broken values (#103804).
import fs from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { afterEach, expect, it } from "vitest";
import { readLaunchAgentProgramArgumentsFromFile } from "./launchd-plist.js";

let tmpDir: string | undefined;

afterEach(async () => {
  if (tmpDir) {
    await fs.rm(tmpDir, { recursive: true, force: true });
    tmpDir = undefined;
  }
});

function buildPlist(programArguments: string[]): string {
  const args = programArguments.map((arg) => `<string>${arg}</string>`).join("\n      ");
  return `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
  <dict>
    <key>Label</key>
    <string>ai.openclaw.gateway</string>
    <key>ProgramArguments</key>
    <array>
      ${args}
    </array>
  </dict>
</plist>
`;
}

it("self-heals JSON-quoted values when reading a generated env file", async () => {
  tmpDir = await fs.mkdtemp(path.join(os.tmpdir(), "openclaw-launchd-env-"));
  const envDir = path.join(tmpDir, "service-env");
  await fs.mkdir(envDir, { recursive: true });
  const label = "ai.openclaw.gateway";
  const envFilePath = path.join(envDir, `${label}.env`);
  const wrapperPath = path.join(envDir, `${label}-env-wrapper.sh`);
  await fs.writeFile(
    envFilePath,
    [
      "# Generated by OpenClaw. Do not edit while the gateway service is installed.",
      // Provenance gate: only keys the file itself declares as OpenClaw-managed
      // are healed; their loaders strip quotes, so wrapping quotes = corruption.
      "export OPENCLAW_SERVICE_MANAGED_ENV_KEYS='AWS_REGION,AWS_ACCESS_KEY_ID'",
      // Corrupted managed entry: JSON quote pair survived inside the shell
      // quotes and previously round-tripped forever through doctor --fix.
      `export AWS_REGION='"us-east-1"'`,
      "export AWS_ACCESS_KEY_ID='test-access-key'",
      // Interior quotes are data, not wrapping - they must be preserved.
      `export QUOTED_INNER='has "quotes" inside'`,
      // Operator-authored (unmanaged) value whose bytes deliberately include
      // wrapping quotes: decoding must stay lossless.
      `export OPERATOR_LITERAL='"secret"'`,
      "",
    ].join("\n"),
    "utf8",
  );
  const plistPath = path.join(tmpDir, `${label}.plist`);
  await fs.writeFile(
    plistPath,
    buildPlist(["/bin/sh", wrapperPath, envFilePath, "/usr/local/bin/openclaw", "gateway", "run"]),
    "utf8",
  );

  const result = await readLaunchAgentProgramArgumentsFromFile(plistPath);

  expect(result?.environment?.AWS_REGION).toBe("us-east-1");
  expect(result?.environment?.AWS_ACCESS_KEY_ID).toBe("test-access-key");
  expect(result?.environment?.QUOTED_INNER).toBe('has "quotes" inside');
  expect(result?.environment?.OPERATOR_LITERAL).toBe('"secret"');
});
