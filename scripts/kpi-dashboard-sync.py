"""KPI Dashboard Sync — JSON 데이터를 읽어 Obsidian 대시보드에 한글 정상 출력"""
import json
import re
import sys
from datetime import datetime
from pathlib import Path

DASHBOARD = Path(r"C:\Users\jini9\OneDrive\Documents\JINI_SYNC\01.PROJECT\_KPI_DASHBOARD.md")
HISTORY_DIR = Path(r"C:\MAIBOT\memory\kpi-history")

def latest_json():
    files = sorted(HISTORY_DIR.glob("*.json"), reverse=True)
    return files[0] if files else None

def build_dashboard(data):
    ts = data.get("collected_at", datetime.now().strftime("%Y-%m-%d"))
    metrics = data.get("metrics", [])
    
    gh_rows = ""
    for m in metrics:
        gh_rows += f"| {m['project']} | {m.get('stars',0)} | {m.get('forks',0)} | {m.get('open_issues',0)} | {m.get('commits_30d',0)} | {m.get('last_activity','N/A')} |\n"

    total_stars = sum(m.get('stars', 0) for m in metrics)
    total_commits = sum(m.get('commits_30d', 0) for m in metrics)
    active = sum(1 for m in metrics if m.get('commits_30d', 0) > 0)

    return f"""# MAI Universe KPI Dashboard

> Last updated: {ts}

<!-- AUTO:kpi-stats:START -->

## 📊 기여 지표 (GitHub)

| Project | Stars | Forks | Issues | Commits(30d) | Last Activity |
|---------|-------|-------|--------|-------------|---------------|
{gh_rows}
## 💰 수익 지표

| Project | 모델 | 상태 | MRR | 비고 |
|---------|------|------|-----|------|
| MAIBOTALKS | ₩3,900/월 구독 | 배포 대기 | ₩0 | Apple/Google 가입 필요 |
| MAIOSS | B2B 라이선스 | 개발중 | ₩0 | MVP 후 파일럿 |
| MAIBEAUTY | B2B SaaS | 개발중 | ₩0 | Railway 배포 |
| MAITUTOR | Freemium | 기획중 | ₩0 | - |
| MAITOK | 광고/구독 | 기획중 | ₩0 | - |

## 🎯 파이프라인 Stage 현황

| Project | Stage | 진행률 | 다음 마일스톤 |
|---------|-------|--------|--------------|
| MAIBOT | Stage 6 (REALIZE) | 90% | KPI 자동화 |
| MAIBEAUTY | Stage 4 (BUILD) | 60% | MVP 완성 |
| MAIOSS | Stage 3 (DESIGN) | 40% | 아키텍처 확정 |
| MAITUTOR | Stage 3 (DESIGN) | 30% | 커리큘럼 설계 |
| MAIBOTALKS | Stage 5 (LAUNCH) | 70% | 앱스토어 제출 |
| MAITOK | Stage 2 (EXPLORE) | 20% | 컨셉 검증 |
| MAIAX | Stage 4 (BUILD) | 50% | 코어 엔진 |
| MAISECONDBRAIN | Stage 4 (BUILD) | 70% | Obsidian 플러그인 |

## 📈 트렌드 (주간 변화)

- 총 Stars: {total_stars}
- 총 Commits(30d): {total_commits}
- 활성 프로젝트(30일내 커밋): {active} / {len(metrics)}

<!-- AUTO:kpi-stats:END -->

---
*Auto-generated by kpi-dashboard-sync.py*
"""

if __name__ == "__main__":
    jf = latest_json()
    if not jf:
        print("No KPI history found")
        sys.exit(1)
    
    data = json.loads(jf.read_text(encoding="utf-8"))
    md = build_dashboard(data)
    DASHBOARD.write_text(md, encoding="utf-8")
    print(f"Dashboard updated: {DASHBOARD}")
