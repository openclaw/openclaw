#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import YAML from "yaml";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "../..");
const manifestsDir = path.join(scriptDir, "manifests");
const kustomizationPath = path.join(manifestsDir, "kustomization.yaml");
const outputPath = path.join(scriptDir, "manifest.yaml");
const outputRelativePath = path.relative(repoRoot, outputPath);
const sourceRelativePath = path.relative(repoRoot, kustomizationPath);

function usage() {
  return "Usage: pnpm k8s:manifest:gen | pnpm k8s:manifest:check";
}

function parseMode(argv) {
  const write = argv.includes("--write");
  const check = argv.includes("--check");
  if (write === check) {
    throw new Error(`Use exactly one of --write or --check.\n\n${usage()}`);
  }
  return write ? "write" : "check";
}

function assertObject(value, label) {
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    throw new Error(`${label} must be a mapping.`);
  }
  return value;
}

function resolveResourcePath(resource) {
  if (typeof resource !== "string" || resource.trim() === "") {
    throw new Error(`${sourceRelativePath} resources must be non-empty file paths.`);
  }
  if (path.isAbsolute(resource) || resource.includes("\0")) {
    throw new Error(`${sourceRelativePath} resource path is not repo-relative: ${resource}`);
  }

  const resolved = path.resolve(manifestsDir, resource);
  const relativeToManifests = path.relative(manifestsDir, resolved);
  if (relativeToManifests.startsWith("..") || path.isAbsolute(relativeToManifests)) {
    throw new Error(
      `${sourceRelativePath} resource path escapes scripts/k8s/manifests: ${resource}`,
    );
  }
  return resolved;
}

async function readKustomizationResources() {
  const raw = await fs.readFile(kustomizationPath, "utf8");
  const kustomization = assertObject(YAML.parse(raw), sourceRelativePath);
  const allowedKeys = new Set(["apiVersion", "kind", "resources"]);
  const unsupportedKeys = Object.keys(kustomization).filter((key) => !allowedKeys.has(key));
  if (unsupportedKeys.length > 0) {
    throw new Error(
      [
        `${sourceRelativePath} uses unsupported kustomization field(s): ${unsupportedKeys.join(", ")}`,
        "Update scripts/k8s/generate-manifest.mjs before relying on generated single-file output.",
      ].join("\n"),
    );
  }

  if (!Array.isArray(kustomization.resources) || kustomization.resources.length === 0) {
    throw new Error(`${sourceRelativePath} must list at least one resource.`);
  }
  return kustomization.resources;
}

export async function generateK8sManifest() {
  const resources = await readKustomizationResources();
  const resourceTexts = await Promise.all(
    resources.map(async (resource) => {
      const resourcePath = resolveResourcePath(resource);
      return (await fs.readFile(resourcePath, "utf8")).trimEnd();
    }),
  );

  return [
    `# Generated by \`pnpm k8s:manifest:gen\` from ${sourceRelativePath}.`,
    "# Do not edit directly; update scripts/k8s/manifests/* and regenerate.",
    "",
    resourceTexts.join("\n---\n"),
    "",
  ].join("\n");
}

async function run(argv = process.argv.slice(2)) {
  const mode = parseMode(argv);
  const generated = await generateK8sManifest();

  if (mode === "write") {
    await fs.writeFile(outputPath, generated);
    console.log(`Wrote ${outputRelativePath}`);
    return;
  }

  let current;
  try {
    current = await fs.readFile(outputPath, "utf8");
  } catch (error) {
    if (error?.code === "ENOENT") {
      current = null;
    } else {
      throw error;
    }
  }
  if (current === generated) {
    console.log(`OK ${outputRelativePath}`);
    return;
  }

  console.error(
    [
      "Kubernetes single-file manifest drift detected.",
      `${outputRelativePath} must be generated from ${sourceRelativePath}.`,
      "Run `pnpm k8s:manifest:gen` and commit the updated manifest.",
    ].join("\n"),
  );
  process.exitCode = 1;
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  try {
    await run();
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
  }
}
