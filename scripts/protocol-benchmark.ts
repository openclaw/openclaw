import { execFileSync, spawnSync } from "node:child_process";
import { promises as fs } from "node:fs";
import path from "node:path";

const repoRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const protoFile = path.join(repoRoot, "dist", "protocol.proto");
const outFile = path.join(repoRoot, "docs", "refactor", "protobuf-benchmark.md");

function hasProtoc(): boolean {
  return spawnSync("bash", ["-lc", "command -v protoc"], { stdio: "ignore" }).status === 0;
}

function toTextProto(value: unknown, indent = 0): string {
  const pad = " ".repeat(indent);
  if (Array.isArray(value)) {
    return value.map((v) => toTextProto(v, indent)).join("\n");
  }
  if (value && typeof value === "object") {
    const lines: string[] = [];
    for (const [k, v] of Object.entries(value as Record<string, unknown>)) {
      if (Array.isArray(v)) {
        for (const item of v) {
          if (item && typeof item === "object") {
            lines.push(`${pad}${k} {`);
            lines.push(toTextProto(item, indent + 2));
            lines.push(`${pad}}`);
          } else {
            lines.push(`${pad}${k}: ${scalar(item)}`);
          }
        }
      } else if (v && typeof v === "object") {
        lines.push(`${pad}${k} {`);
        lines.push(toTextProto(v, indent + 2));
        lines.push(`${pad}}`);
      } else {
        lines.push(`${pad}${k}: ${scalar(v)}`);
      }
    }
    return lines.join("\n");
  }
  return `${pad}${scalar(value)}`;
}

function scalar(v: unknown): string {
  if (typeof v === "string") {
    return JSON.stringify(v);
  }
  if (typeof v === "boolean") {
    return v ? "true" : "false";
  }
  if (typeof v === "number") {
    return Number.isFinite(v) ? String(v) : "0";
  }
  if (v === null || typeof v === "undefined") {
    return '""';
  }
  return '""';
}

function encodeLenSync(messageType: string, payload: Record<string, unknown>): number {
  const text = `${toTextProto(payload)}\n`;
  const encoded = execFileSync(
    "protoc",
    [`--proto_path=${path.join(repoRoot, "dist")}`, `--encode=${messageType}`, protoFile],
    {
      input: text,
      stdio: ["pipe", "pipe", "pipe"],
    },
  );
  return encoded.length;
}

function jsonLen(payload: unknown): number {
  return Buffer.byteLength(JSON.stringify(payload), "utf8");
}

async function main() {
  if (!hasProtoc()) {
    throw new Error("protoc not found; cannot run benchmark");
  }

  const samples: Array<{ name: string; type: string; payload: Record<string, unknown> }> = [
    {
      name: "RequestFrame (health)",
      type: "openclaw.gateway.v1.RequestFrame",
      payload: {
        type: "req",
        id: "h1",
        method: "health",
      },
    },
    {
      name: "ResponseFrame (ok)",
      type: "openclaw.gateway.v1.ResponseFrame",
      payload: {
        type: "res",
        id: "h1",
        ok: true,
      },
    },
    {
      name: "EventFrame tick",
      type: "openclaw.gateway.v1.EventFrame",
      payload: {
        type: "event",
        event: "tick",
        seq: 1203,
      },
    },
  ];

  const rows = samples.map((s) => {
    const j = jsonLen(s.payload);
    const p = encodeLenSync(s.type, s.payload);
    const savings = j > 0 ? (((j - p) / j) * 100).toFixed(1) : "0.0";
    return { ...s, jsonBytes: j, protoBytes: p, savings };
  });

  const avgSavings =
    (rows.reduce((acc, r) => acc + (r.jsonBytes - r.protoBytes) / r.jsonBytes, 0) / rows.length) *
    100;

  const md = [
    "# Protobuf vs JSON size benchmark (sample frames)",
    "",
    "Generated by `scripts/protocol-benchmark.ts`.",
    "",
    "| Sample | JSON bytes | Proto bytes | Reduction |",
    "|---|---:|---:|---:|",
    ...rows.map((r) => `| ${r.name} | ${r.jsonBytes} | ${r.protoBytes} | ${r.savings}% |`),
    "",
    `Average reduction across samples: **${avgSavings.toFixed(1)}%**`,
    "",
    "## Notes",
    "",
    "- This benchmark measures wire-size reduction for representative frames.",
    "- Current runtime remains JSON-over-WS for compatibility.",
    "- Protobuf transport rollout should use negotiated dual-stack migration.",
  ].join("\n");

  await fs.writeFile(outFile, `${md}\n`, "utf8");
  console.log(`wrote ${outFile}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
