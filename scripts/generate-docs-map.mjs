#!/usr/bin/env node

/**
 * Generates docs/docs_map.md — a structured outline of every doc page's
 * headings so LLM agents can discover relevant pages from a single file.
 *
 * Usage: node scripts/generate-docs-map.mjs
 * Output: docs/docs_map.md
 */

import { existsSync, readdirSync, readFileSync, writeFileSync, statSync } from "node:fs";
import { join, relative } from "node:path";

const DOCS_DIR = join(process.cwd(), "docs");
const OUTPUT_FILE = join(DOCS_DIR, "docs_map.md");

if (!existsSync(DOCS_DIR) || !statSync(DOCS_DIR).isDirectory()) {
  console.error("generate-docs-map: missing docs directory. Run from repo root.");
  process.exit(1);
}

const EXCLUDED_DIRS = new Set(["archive", "research", "internal", ".i18n", "images", "assets"]);

/**
 * Walk markdown files, skipping excluded directories.
 */
function walkMarkdownFiles(dir, base = dir) {
  const entries = readdirSync(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    if (entry.name.startsWith(".")) {
      continue;
    }
    const fullPath = join(dir, entry.name);
    if (entry.isDirectory()) {
      if (EXCLUDED_DIRS.has(entry.name)) {
        continue;
      }
      files.push(...walkMarkdownFiles(fullPath, base));
    } else if (entry.isFile() && (entry.name.endsWith(".md") || entry.name.endsWith(".mdx"))) {
      if (entry.name === "docs_map.md") {
        continue;
      } // skip self
      files.push(relative(base, fullPath));
    }
  }
  return files.toSorted((a, b) => a.localeCompare(b));
}

/**
 * Extract title from front matter and H2/H3/H4 headings from content.
 */
function extractPageInfo(fullPath) {
  const content = readFileSync(fullPath, "utf8");
  let body = content;
  let title = null;

  // Parse front matter for title
  if (content.startsWith("---")) {
    const endIndex = content.indexOf("\n---", 3);
    if (endIndex !== -1) {
      const frontMatter = content.slice(3, endIndex).trim();
      body = content.slice(endIndex + 4);
      for (const line of frontMatter.split("\n")) {
        const trimmed = line.trim();
        if (trimmed.startsWith("title:")) {
          title = trimmed
            .slice("title:".length)
            .trim()
            .replace(/^['"]|['"]$/g, "");
        }
      }
    }
  }

  // Extract H2/H3/H4 headings from body, skipping fenced code blocks
  const headings = [];
  let inFence = false;
  for (const line of body.split("\n")) {
    if (line.startsWith("```")) {
      inFence = !inFence;
      continue;
    }
    if (inFence) {
      continue;
    }
    const match = line.match(/^(#{2,4})\s+(.+)$/);
    if (match) {
      const level = match[1].length; // 2, 3, or 4
      const text = match[2].trim();
      headings.push({ level, text });
    }
  }

  return { title, headings };
}

/**
 * Build directory tree structure from file list.
 */
function buildTree(files) {
  const tree = {};
  for (const file of files) {
    const parts = file.split("/");
    const dir = parts.length > 1 ? parts.slice(0, -1).join("/") : "";
    if (!tree[dir]) {
      tree[dir] = [];
    }
    tree[dir].push(file);
  }
  return tree;
}

// --- Main ---

const markdownFiles = walkMarkdownFiles(DOCS_DIR);
const tree = buildTree(markdownFiles);

const lines = [];
const seenHeadings = new Set();
lines.push("# Docs Map");
lines.push("");
lines.push(
  "Auto-generated navigation map for LLM agents. Each page lists its H2/H3/H4 headings so you can find relevant content without reading every file.",
);
lines.push("");
lines.push(
  `> Generated by \`node scripts/generate-docs-map.mjs\` — ${markdownFiles.length} pages.`,
);
lines.push("");

// Sort directories for consistent output
const sortedDirs = Object.keys(tree).toSorted((a, b) => {
  // Root files first, then alphabetical
  if (a === "") {
    return -1;
  }
  if (b === "") {
    return 1;
  }
  return a.localeCompare(b);
});

for (const dir of sortedDirs) {
  if (dir) {
    lines.push(`## ${dir}/`);
  } else {
    lines.push("## Pages");
  }
  lines.push("");

  for (const file of tree[dir]) {
    const fullPath = join(DOCS_DIR, file);
    const { title, headings } = extractPageInfo(fullPath);

    // Page header: use front-matter title or filename
    let displayTitle = title || file.replace(/\.(md|mdx)$/, "");
    // Append directory context for duplicate headings (MD024)
    if (seenHeadings.has(displayTitle)) {
      displayTitle = dir ? `${displayTitle} (${dir})` : `${displayTitle} (${file})`;
    }
    seenHeadings.add(displayTitle);
    lines.push(`### ${displayTitle}`);
    lines.push("");
    lines.push(`\`${file}\``);
    lines.push("");

    if (headings.length > 0) {
      for (const h of headings) {
        const indent = "  ".repeat(h.level - 2);
        lines.push(`${indent}- ${h.text}`);
      }
      lines.push("");
    }
  }
}

writeFileSync(OUTPUT_FILE, lines.join("\n"), "utf8");
console.log(`generate-docs-map: wrote ${OUTPUT_FILE} (${markdownFiles.length} pages)`);
