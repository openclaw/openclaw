# KPI Report Generator - MAI Universe
# Runs kpi-collector, compares with previous week, updates Obsidian dashboard
# Usage: .\kpi-report.ps1

$ErrorActionPreference = "Stop"

$scriptDir = Split-Path -Parent $MyInvocation.MyCommand.Path
$memoryDir = "C:\MAIBOT\memory"
$historyDir = "$memoryDir\kpi-history"
$dashboardPath = "C:\Users\jini9\OneDrive\Documents\JINI_SYNC\01.PROJECT\_KPI_DASHBOARD.md"
$today = (Get-Date).ToString("yyyy-MM-dd")
$jsonPath = "$historyDir\$today.json"

# Ensure dirs
if (-not (Test-Path $historyDir)) { New-Item -ItemType Directory -Path $historyDir -Force | Out-Null }

# 1. Run collector
Write-Host "=== Running KPI Collector ===" -ForegroundColor Green
$raw = & "$scriptDir\kpi-collector.ps1" -OutputJson $jsonPath
$data = Get-Content $jsonPath -Raw | ConvertFrom-Json

# 2. Find previous report for delta
$prevFiles = Get-ChildItem "$historyDir\*.json" | Where-Object { $_.BaseName -lt $today } | Sort-Object Name -Descending
$prevData = $null
if ($prevFiles.Count -gt 0) {
    $prevData = Get-Content $prevFiles[0].FullName -Raw | ConvertFrom-Json
    Write-Host "Previous report: $($prevFiles[0].Name)" -ForegroundColor Cyan
}

# 3. Build dashboard markdown
function Delta($current, $previous) {
    if ($null -eq $previous) { return "" }
    $diff = $current - $previous
    if ($diff -gt 0) { return " (+$diff)" }
    elseif ($diff -lt 0) { return " ($diff)" }
    return ""
}

function FindPrev($proj, $field) {
    if (-not $prevData) { return $null }
    $m = $prevData.metrics | Where-Object { $_.project -eq $proj }
    if ($m) { return $m.$field }
    return $null
}

$ghRows = ""
foreach ($m in $data.metrics) {
    $p = $m.project
    $starsD = Delta $m.stars (FindPrev $p "stars")
    $forksD = Delta $m.forks (FindPrev $p "forks")
    $commitsD = Delta $m.commits_30d (FindPrev $p "commits_30d")
    $activity = if ($m.last_activity) { $m.last_activity } else { "N/A" }
    $err = if ($m.error) { " ⚠️" } else { "" }
    $ghRows += "| $p$err | $($m.stars)$starsD | $($m.forks)$forksD | $($m.open_issues) | $($m.commits_30d)$commitsD | $activity |`n"
}

# Revenue section (from revenue-tracker if exists)
$revenueSection = @"
