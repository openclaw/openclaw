|---------|------|------|-----|------|
| MAIBOTALKS | ₩3,900/월 구독 | 배포 대기 | ₩0 | Apple/Google 가입 필요 |
| MAIOSS | B2B 라이선스 | 개발중 | ₩0 | MVP 후 파일럿 |
| MAIBEAUTY | B2B SaaS | 개발중 | ₩0 | Railway 배포 |
| MAITUTOR | Freemium | 기획중 | ₩0 | - |
| MAITOK | 광고/구독 | 기획중 | ₩0 | - |
"@

# Pipeline stages
$pipelineSection = @"
| Project | Stage | 진행률 | 다음 마일스톤 |
|---------|-------|--------|--------------|
| MAIBOT | Stage 6 (REALIZE) | 90% | KPI 자동화 |
| MAIBEAUTY | Stage 4 (BUILD) | 60% | MVP 완성 |
| MAIOSS | Stage 3 (DESIGN) | 40% | 아키텍처 확정 |
| MAITUTOR | Stage 3 (DESIGN) | 30% | 커리큘럼 설계 |
| MAIBOTALKS | Stage 5 (LAUNCH) | 70% | 앱스토어 제출 |
| MAITOK | Stage 2 (EXPLORE) | 20% | 컨셉 검증 |
| MAIAX | Stage 4 (BUILD) | 50% | 코어 엔진 |
| MAISECONDBRAIN | Stage 2 (EXPLORE) | 15% | 프로토타입 |
