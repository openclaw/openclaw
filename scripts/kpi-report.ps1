$dashboard = @"
# MAI Universe KPI Dashboard

> Last updated: $($data.collected_at)

<!-- AUTO:kpi-stats:START -->

## 📊 기여 지표 (GitHub)

| Project | Stars | Forks | Issues | Commits(30d) | Last Activity |
|---------|-------|-------|--------|-------------|---------------|
$ghRows
## 💰 수익 지표

$revenueSection

## 🎯 파이프라인 Stage 현황

$pipelineSection

## 📈 트렌드 (주간 변화)

$(if ($prevData) { "- 이전 수집: $($prevData.collected_at)" } else { "- 첫 수집 — 다음 주부터 변화 추적" })
- 총 Stars: $(($data.metrics | Measure-Object -Property stars -Sum).Sum)
- 총 Commits(30d): $(($data.metrics | Measure-Object -Property commits_30d -Sum).Sum)
- 활성 프로젝트(30일내 커밋): $(($data.metrics | Where-Object { $_.commits_30d -gt 0 }).Count) / $($data.metrics.Count)

<!-- AUTO:kpi-stats:END -->

---
*Auto-generated by kpi-report.ps1 | [revenue-tracker](revenue-tracker.md) for manual revenue updates*
"@

# 4. Write dashboard
$dashDir = Split-Path $dashboardPath -Parent
if (-not (Test-Path $dashDir)) { New-Item -ItemType Directory -Path $dashDir -Force | Out-Null }
[System.IO.File]::WriteAllText($dashboardPath, $dashboard, [System.Text.UTF8Encoding]::new($false))
Write-Host "`nDashboard updated: $dashboardPath" -ForegroundColor Green
Write-Host "JSON saved: $jsonPath" -ForegroundColor Green
Write-Host "=== KPI Report Complete ===" -ForegroundColor Green
