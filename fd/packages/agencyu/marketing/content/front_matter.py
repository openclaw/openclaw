"""Front matter header for generated content artifacts.

Every content artifact generated by OpenClaw must carry a YAML front-matter
header that declares its brand, offer, angle, voice profile, and content type.

If the header is missing or invalid, the content is considered INVALID and
must not be published.
"""
from __future__ import annotations

from datetime import UTC, datetime
from typing import Any

import yaml

from packages.agencyu.marketing.content.spec import ContentSpec


def render_front_matter(spec: ContentSpec, correlation_id: str = "") -> str:
    """Render a YAML front-matter header from a ContentSpec.

    Output format:
        ---
        brand: fulldigital
        offer_id: major_label_simulation
        angle_id: era_control
        voice_profile_id: fd_exec_authority
        content_type: landing_page
        generated_at: 2026-03-01T...
        correlation_id: ...
        ---
    """
    doc: dict[str, Any] = {
        "brand": spec.brand,
        "offer_id": spec.offer_id,
        "angle_id": spec.angle_id,
        "voice_profile_id": spec.voice_profile_id,
        "content_type": spec.content_type,
        "generated_at": datetime.now(UTC).isoformat(),
    }
    if spec.variant_id:
        doc["variant_id"] = spec.variant_id
    if correlation_id:
        doc["correlation_id"] = correlation_id

    return "---\n" + yaml.safe_dump(doc, sort_keys=False).strip() + "\n---\n"


def parse_front_matter(text: str) -> dict[str, Any] | None:
    """Parse a YAML front-matter header from content text.

    Returns the parsed dict, or None if no valid front matter found.
    """
    text = text.strip()
    if not text.startswith("---"):
        return None

    end_idx = text.find("---", 3)
    if end_idx == -1:
        return None

    yaml_block = text[3:end_idx].strip()
    try:
        return yaml.safe_load(yaml_block) or None
    except yaml.YAMLError:
        return None


def has_valid_front_matter(text: str) -> bool:
    """Check whether content has a valid front-matter header with required fields."""
    fm = parse_front_matter(text)
    if fm is None:
        return False

    required = {"brand", "offer_id", "angle_id", "voice_profile_id", "content_type"}
    return required.issubset(fm.keys())
