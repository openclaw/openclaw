#!/bin/bash
# Install Kimi ACP Agent Adapter for OpenClaw

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ADAPTER_SCRIPT="$SCRIPT_DIR/kimi-adapter.js"

# Check if Kimi is installed
if ! command -v kimi &> /dev/null; then
    echo "Error: Kimi CLI is not installed. Please install it first:"
    echo "  https://www.kimi.com/"
    exit 1
fi

echo "✓ Kimi CLI found: $(kimi --version)"

# Find acpx binary
ACPX_BIN=""
if command -v acpx &> /dev/null; then
    ACPX_BIN="$(which acpx)"
elif [ -f "/usr/lib/node_modules/openclaw/extensions/acpx/node_modules/.bin/acpx" ]; then
    ACPX_BIN="/usr/lib/node_modules/openclaw/extensions/acpx/node_modules/.bin/acpx"
else
    echo "Error: acpx binary not found"
    exit 1
fi

echo "✓ acpx found: $ACPX_BIN"

# Create symlink or wrapper
INSTALL_DIR="$(dirname "$ACPX_BIN")"
KIMI_LINK="$INSTALL_DIR/kimi"

if [ -L "$KIMI_LINK" ] || [ -f "$KIMI_LINK" ]; then
    echo "Backing up existing kimi link/script..."
    mv "$KIMI_LINK" "$KIMI_LINK.backup.$(date +%s)"
fi

# Create a wrapper script that calls our adapter
cat > "$KIMI_LINK" << EOF
#!/bin/bash
# Kimi ACP Agent Adapter Wrapper
# Generated by install.sh

exec node "$ADAPTER_SCRIPT" "\$@"
EOF

chmod +x "$KIMI_LINK"

echo "✓ Installed kimi adapter to: $KIMI_LINK"
echo ""
echo "Testing installation..."

# Test
if "$KIMI_LINK" status > /dev/null 2>&1; then
    echo "✓ Adapter test passed"
else
    echo "⚠ Adapter test failed - check manually"
fi

echo ""
echo "Installation complete!"
echo ""
echo "You can now use Kimi as an ACP agent:"
echo "  acpx kimi 'Write a Python function'"
echo "  acpx kimi sessions new --name my-session"
echo ""
echo "Or from OpenClaw:"
echo '  sessions_spawn({'
echo '    runtime: "acp",'
echo '    agentId: "kimi",'
echo '    task: "Write a Python function"'
echo '  })'