import fs from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import type { ResolvedAcpxPluginConfig } from "./config.js";

const CODEX_ACP_PACKAGE = "@zed-industries/codex-acp";
const CODEX_ACP_PACKAGE_RANGE = "^0.11.1";
const CODEX_ACP_BIN = "codex-acp";

function quoteCommandPart(value: string): string {
  return JSON.stringify(value);
}

function tomlString(value: string): string {
  return JSON.stringify(value);
}

function buildCodexAcpWrapperScript(): string {
  return `#!/usr/bin/env node
import { existsSync } from "node:fs";
import path from "node:path";
import { spawn } from "node:child_process";
import { fileURLToPath } from "node:url";

const codexHome = fileURLToPath(new URL("./codex-home/", import.meta.url));
const env = {
  ...process.env,
  CODEX_HOME: codexHome,
};
const configuredArgs = process.argv.slice(2);

function resolveNpmCliPath() {
  const candidate = path.resolve(
    path.dirname(process.execPath),
    "..",
    "lib",
    "node_modules",
    "npm",
    "bin",
    "npm-cli.js",
  );
  return existsSync(candidate) ? candidate : undefined;
}

const npmCliPath = resolveNpmCliPath();
const defaultCommand = npmCliPath ? process.execPath : process.platform === "win32" ? "npx.cmd" : "npx";
const defaultArgs = npmCliPath
  ? [npmCliPath, "exec", "--yes", "--package", "${CODEX_ACP_PACKAGE}@${CODEX_ACP_PACKAGE_RANGE}", "--", "${CODEX_ACP_BIN}"]
  : ["--yes", "--package", "${CODEX_ACP_PACKAGE}@${CODEX_ACP_PACKAGE_RANGE}", "--", "${CODEX_ACP_BIN}"];
const command = configuredArgs[0] ?? defaultCommand;
const args = configuredArgs.length > 0 ? configuredArgs.slice(1) : defaultArgs;

const child = spawn(command, args, {
  env,
  stdio: "inherit",
  windowsHide: true,
});

for (const signal of ["SIGINT", "SIGTERM", "SIGHUP"]) {
  process.once(signal, () => {
    child.kill(signal);
  });
}

child.on("error", (error) => {
  console.error(\`[openclaw] failed to launch isolated Codex ACP wrapper: \${error.message}\`);
  process.exit(1);
});

child.on("exit", (code, signal) => {
  if (code !== null) {
    process.exit(code);
  }
  process.exit(signal ? 1 : 0);
});
`;
}

function parseTrustedCodexProjectPaths(configText: string): string[] {
  const trustedPaths: string[] = [];
  let currentProjectPath: string | null = null;
  let currentProjectTrusted = false;

  const flush = () => {
    if (currentProjectPath && currentProjectTrusted) {
      trustedPaths.push(currentProjectPath);
    }
    currentProjectPath = null;
    currentProjectTrusted = false;
  };

  for (const line of configText.split(/\r?\n/)) {
    const section = /^\s*\[projects\.("(?:(?:\\.)|[^"\\])*")\]\s*$/.exec(line);
    if (section) {
      flush();
      try {
        currentProjectPath = JSON.parse(section[1] ?? "") as string;
      } catch {
        currentProjectPath = null;
      }
      continue;
    }
    if (/^\s*\[/.test(line)) {
      flush();
      continue;
    }
    if (currentProjectPath && /^\s*trust_level\s*=\s*["']trusted["']\s*$/.test(line)) {
      currentProjectTrusted = true;
    }
  }
  flush();

  return trustedPaths;
}

function resolveSourceCodexConfigPath(): string {
  const configuredCodexHome = process.env.CODEX_HOME?.trim();
  return path.join(configuredCodexHome || path.join(os.homedir(), ".codex"), "config.toml");
}

async function readTrustedCodexProjectPaths(): Promise<string[]> {
  try {
    const sourceConfig = await fs.readFile(resolveSourceCodexConfigPath(), "utf8");
    return parseTrustedCodexProjectPaths(sourceConfig);
  } catch {
    return [];
  }
}

function renderIsolatedCodexConfig(trustedProjectPaths: string[]): string {
  const uniqueTrustedPaths = [
    ...new Set(trustedProjectPaths.map((entry) => entry.trim()).filter(Boolean)),
  ];
  return [
    "# Generated by OpenClaw for Codex ACP sessions.",
    "# Only trusted project declarations are copied into this isolated Codex home.",
    "",
    ...uniqueTrustedPaths.flatMap((projectPath) => [
      `[projects.${tomlString(projectPath)}]`,
      'trust_level = "trusted"',
      "",
    ]),
  ].join("\n");
}

async function prepareIsolatedCodexHome(
  baseDir: string,
  trustedProjectPaths: string[],
): Promise<string> {
  const codexHome = path.join(baseDir, "codex-home");
  await fs.mkdir(codexHome, { recursive: true });
  await fs.writeFile(
    path.join(codexHome, "config.toml"),
    renderIsolatedCodexConfig(trustedProjectPaths),
    "utf8",
  );
  return codexHome;
}

async function writeCodexAcpWrapper(baseDir: string): Promise<string> {
  await fs.mkdir(baseDir, { recursive: true });
  const wrapperPath = path.join(baseDir, "codex-acp-wrapper.mjs");
  await fs.writeFile(wrapperPath, buildCodexAcpWrapperScript(), {
    encoding: "utf8",
  });
  await fs.chmod(wrapperPath, 0o755);
  return wrapperPath;
}

function buildCodexAcpWrapperCommand(wrapperPath: string, configuredCommand?: string): string {
  const baseCommand = `${quoteCommandPart(process.execPath)} ${quoteCommandPart(wrapperPath)}`;
  const trimmedConfiguredCommand = configuredCommand?.trim();
  // ACPX stores agent commands as shell-like strings and splits them before spawn.
  return trimmedConfiguredCommand ? `${baseCommand} ${trimmedConfiguredCommand}` : baseCommand;
}

export async function prepareAcpxCodexAuthConfig(params: {
  pluginConfig: ResolvedAcpxPluginConfig;
  stateDir: string;
  logger?: unknown;
}): Promise<ResolvedAcpxPluginConfig> {
  void params.logger;
  const codexBaseDir = path.join(params.stateDir, "acpx");
  const inheritedTrustedProjects = await readTrustedCodexProjectPaths();
  await prepareIsolatedCodexHome(codexBaseDir, [
    ...inheritedTrustedProjects,
    params.pluginConfig.cwd,
  ]);
  const wrapperPath = await writeCodexAcpWrapper(codexBaseDir);
  const configuredCodexCommand = params.pluginConfig.agents.codex;

  return {
    ...params.pluginConfig,
    agents: {
      ...params.pluginConfig.agents,
      codex: buildCodexAcpWrapperCommand(wrapperPath, configuredCodexCommand),
    },
  };
}
