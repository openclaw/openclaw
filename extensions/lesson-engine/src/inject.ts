import * as fs from "node:fs";
import * as path from "node:path";
import type { AgentName, Lesson, LessonsFile, Severity } from "./types.js";
import { agentDataRoot, lessonsFilePath, nowIso, readJson } from "./utils.js";

// ── Types ──

export interface InjectOptions {
  agent: AgentName;
  domainTags?: string[];
  maxTokens?: number; // default 600
  now?: Date;
  root?: string;
  dryRun?: boolean;
}

export interface InjectedLesson {
  id: string;
  title: string;
  tags: string[];
  lesson: string;
  fix?: string;
  severity: Severity;
  hitCount: number;
}

export interface InjectResult {
  agent: AgentName;
  selected: InjectedLesson[];
  totalLessons: number;
  estimatedTokens: number;
  outputPath: string | null;
}

// ── Constants ──

const DEFAULT_MAX_TOKENS = 600;
const MAX_LESSONS = 3;

const SEVERITY_ORDER: Record<Severity, number> = {
  critical: 0,
  high: 1,
  important: 2,
  minor: 3,
};

// ── Helpers ──

/** Estimate token count: ceil(text.length / 4). */
export function estimateTokens(text: string): number {
  return Math.ceil(text.length / 4);
}

function lessonText(l: Lesson): string {
  const parts: string[] = [];
  if (l.title) parts.push(l.title);
  if (l.lesson) parts.push(String(l.lesson));
  if (l.fix) parts.push(String(l.fix));
  return parts.join(" ");
}

function matchesDomainTags(lessonTags: string[], domainTags: string[]): boolean {
  for (const lt of lessonTags) {
    for (const dt of domainTags) {
      if (lt === dt || lt.startsWith(dt) || dt.startsWith(lt)) return true;
    }
  }
  return false;
}

function injectedLessonsPath(agent: string, root?: string): string {
  return path.join(agentDataRoot(root), agent, "memory", "injected-lessons.md");
}

function renderMarkdown(
  agent: AgentName,
  selected: InjectedLesson[],
  tokens: number,
  now: Date,
): string {
  const lines: string[] = [];
  lines.push(
    `<!-- injected-lessons: auto-generated by lesson-engine inject. Do not edit manually. -->`,
  );
  lines.push(`<!-- agent: ${agent} | generated: ${nowIso(now)} | tokens: ~${tokens} -->`);
  lines.push("");
  lines.push("## 注入教训（Top 3 / Active）");
  lines.push("");

  for (const item of selected) {
    lines.push(`### [${item.severity}] ${item.title}`);
    if (item.tags.length > 0) {
      lines.push(`Tags: ${item.tags.map((t) => `\`${t}\``).join(", ")}`);
    }
    lines.push(`> ${item.lesson}`);
    if (item.fix) {
      lines.push(`Fix: ${item.fix}`);
    }
    lines.push("");
  }

  return lines.join("\n");
}

// ── Main ──

export function injectLessons(opts: InjectOptions): InjectResult {
  const maxTokens = opts.maxTokens ?? DEFAULT_MAX_TOKENS;
  const now = opts.now ?? new Date();
  const dryRun = opts.dryRun ?? false;

  const filePath = lessonsFilePath(opts.agent, opts.root);

  // 1. Read lessons
  let lessons: Lesson[] = [];
  if (fs.existsSync(filePath)) {
    const file = readJson<LessonsFile>(filePath);
    lessons = Array.isArray(file.lessons) ? file.lessons : [];
  }

  // 2. Filter: active only
  let active = lessons.filter((l) => l.lifecycle === "active");
  const totalLessons = active.length;

  // 3. Filter by domainTags if provided
  if (opts.domainTags && opts.domainTags.length > 0) {
    active = active.filter((l) => {
      const tags = Array.isArray(l.tags) ? l.tags : [];
      return matchesDomainTags(tags, opts.domainTags!);
    });
  }

  // 4. Sort: severity priority (critical > high > important > minor), then hitCount desc
  active.sort((a, b) => {
    const sa = SEVERITY_ORDER[a.severity] ?? 99;
    const sb = SEVERITY_ORDER[b.severity] ?? 99;
    if (sa !== sb) return sa - sb;
    return (b.hitCount ?? 0) - (a.hitCount ?? 0);
  });

  // 5. Greedy selection: up to MAX_LESSONS, within maxTokens
  const selected: InjectedLesson[] = [];
  let estimatedTotal = 0;

  for (const l of active) {
    if (selected.length >= MAX_LESSONS) break;
    const text = lessonText(l);
    const tokens = estimateTokens(text);
    if (estimatedTotal + tokens > maxTokens) continue;
    estimatedTotal += tokens;
    selected.push({
      id: l.id,
      title: l.title ?? l.id,
      tags: Array.isArray(l.tags) ? l.tags : [],
      lesson: typeof l.lesson === "string" ? l.lesson : "",
      fix: typeof l.fix === "string" ? l.fix : undefined,
      severity: l.severity,
      hitCount: l.hitCount ?? 0,
    });
  }

  // 6. Write output
  let outputPath: string | null = null;
  if (!dryRun && selected.length > 0) {
    const outPath = injectedLessonsPath(opts.agent, opts.root);
    const dir = path.dirname(outPath);
    fs.mkdirSync(dir, { recursive: true });
    const md = renderMarkdown(opts.agent, selected, estimatedTotal, now);
    fs.writeFileSync(outPath, md, "utf8");
    outputPath = outPath;
  } else if (!dryRun && selected.length === 0) {
    // Write empty file to clear stale injections
    const outPath = injectedLessonsPath(opts.agent, opts.root);
    const dir = path.dirname(outPath);
    fs.mkdirSync(dir, { recursive: true });
    const md = renderMarkdown(opts.agent, selected, 0, now);
    fs.writeFileSync(outPath, md, "utf8");
    outputPath = outPath;
  }

  return {
    agent: opts.agent,
    selected,
    totalLessons,
    estimatedTokens: estimatedTotal,
    outputPath,
  };
}
