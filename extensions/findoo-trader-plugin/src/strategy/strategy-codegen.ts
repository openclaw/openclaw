function templateRegimeAdaptive(p: Record<string, unknown>): string {
  const fastEma = p.fastEma ?? 12;
  const slowEma = p.slowEma ?? 26;
  const bbPeriod = p.bbPeriod ?? 20;
  const bbStdDev = p.bbStdDev ?? 2.0;
  const rsiPeriod = p.rsiPeriod ?? 14;
  const rsiOversoldMR = p.rsiOversoldMR ?? 30;
  const rsiOverboughtMR = p.rsiOverboughtMR ?? 70;
  const bandWidthThreshold = p.bandWidthThreshold ?? 0.04;
  return `${HEADER}
def compute(data):
    df = pd.DataFrame(data)
    df['ema_fast'] = df['close'].ewm(span=${fastEma}, adjust=False).mean()
    df['ema_slow'] = df['close'].ewm(span=${slowEma}, adjust=False).mean()
    df['sma'] = df['close'].rolling(${bbPeriod}).mean()
    df['std'] = df['close'].rolling(${bbPeriod}).std()
    df['upper'] = df['sma'] + ${bbStdDev} * df['std']
    df['lower'] = df['sma'] - ${bbStdDev} * df['std']
    df['bw'] = (df['upper'] - df['lower']) / df['sma']

    delta = df['close'].diff()
    gain = delta.clip(lower=0).rolling(${rsiPeriod}).mean()
    loss = (-delta.clip(upper=0)).rolling(${rsiPeriod}).mean()
    rs = gain / loss.replace(0, np.nan)
    df['rsi'] = 100 - (100 / (1 + rs))

    # Regime detection: trending if bandwidth > threshold
    df['trending'] = df['bw'] > ${bandWidthThreshold}

    df['signal'] = 0
    # Trending: follow EMA crossover
    df.loc[df['trending'] & (df['ema_fast'] > df['ema_slow']) & (df['ema_fast'].shift(1) <= df['ema_slow'].shift(1)), 'signal'] = 1
    df.loc[df['trending'] & (df['ema_fast'] < df['ema_slow']), 'signal'] = -1
    # Mean-reverting: RSI extremes
    df.loc[~df['trending'] & (df['rsi'] < ${rsiOversoldMR}), 'signal'] = 1
    df.loc[~df['trending'] & (df['rsi'] > ${rsiOverboughtMR}), 'signal'] = -1

    df['position'] = df['signal'].replace(0, np.nan).ffill().fillna(0)
    df['returns'] = df['close'].pct_change() * df['position'].shift(1)
    df['equity'] = (1 + df['returns'].fillna(0)).cumprod() * 10000

    trades = []
    pos = 0
    for i, row in df.iterrows():
        if row['signal'] == 1 and pos <= 0:
            reason = 'EMA cross (trend)' if row['trending'] else 'RSI oversold (mean-rev)'
            trades.append({'date': str(row.get('date', i)), 'action': 'buy', 'price': row['close'], 'amount': 1, 'reason': reason})
            pos = 1
        elif row['signal'] == -1 and pos >= 0:
            reason = 'EMA bearish (trend)' if row['trending'] else 'RSI overbought (mean-rev)'
            trades.append({'date': str(row.get('date', i)), 'action': 'sell', 'price': row['close'], 'amount': 1, 'reason': reason})
            pos = -1

    return {
        'equity_curve': [{'date': str(row.get('date', i)), 'equity': row['equity']} for i, row in df.dropna(subset=['equity']).iterrows()],
        'trade_journal': trades,
    }
`;
}

