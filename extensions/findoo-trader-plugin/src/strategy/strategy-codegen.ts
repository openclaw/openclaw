/**
 * strategy-codegen.ts — Generate FEP v1.1 Python strategy ZIP from TS StrategyDefinition.
 *
 * Each builtin strategy type maps to a Python `compute(data)` template.
 * Parameters from the TS definition are substituted into the template.
 * The output is an in-memory ZIP buffer ready for upload to the remote backtest agent.
 */

import JSZip from "jszip";
import type { StrategyDefinition } from "./types.js";

export interface CodegenOptions {
  symbol?: string;
  startDate?: string;
  endDate?: string;
}

export async function generateStrategyZip(
  definition: StrategyDefinition,
  options?: CodegenOptions,
): Promise<{ buffer: Buffer; filename: string }> {
  const strategyId = definition.id;
  const strategyType = extractType(strategyId);
  const params = definition.parameters ?? {};

  const pythonCode = generatePythonStrategy(strategyType, params, definition);
  const fepYaml = generateFepYaml(definition, options);
  const requirements = "pandas>=1.5\nnumpy>=1.24\n";

  const zip = new JSZip();
  const folder = zip.folder(strategyId)!;
  folder.file("fep.yaml", fepYaml);
  const scripts = folder.folder("scripts")!;
  scripts.file("strategy.py", pythonCode);
  scripts.file("requirements.txt", requirements);

  const buf = await zip.generateAsync({ type: "nodebuffer", compression: "DEFLATE" });
  const filename = `${strategyId}-${Date.now()}.zip`;

  return { buffer: buf, filename };
}

/** Extract strategy type prefix from an ID like "sma-crossover-1234567890" or "sma-crossover-mmd7gimr" */
function extractType(id: string): string {
  // Remove timestamp suffix — decimal digits (Date.now()) or base-36 (Date.now().toString(36))
  const match = id.match(/^(.+?)-[a-z0-9]{6,13}$/);
  return match ? match[1] : id;
}

function generateFepYaml(def: StrategyDefinition, opts?: CodegenOptions): string {
  const symbol = opts?.symbol ?? def.symbols[0] ?? "BTC-USD";
  const timeframe = def.timeframes[0] ?? "1d";
  const market = def.markets[0] ?? "crypto";

  return `identity:
  id: ${def.id}
  name: ${def.name}
  version: ${def.version ?? "1.0.0"}

technical:
  asset_class: ${market}
  timeframe: ${timeframe}
  strategy_type: quantitative
  symbols: [${symbol}]

backtest:
  initial_capital: 10000
  benchmark: ${symbol}
${opts?.startDate ? `  start_date: ${opts.startDate}\n` : ""}${opts?.endDate ? `  end_date: ${opts.endDate}\n` : ""}`;
}

function generatePythonStrategy(
  strategyType: string,
  params: Record<string, unknown>,
  def: StrategyDefinition,
): string {
  const templates: Record<string, (p: Record<string, unknown>, d: StrategyDefinition) => string> = {
    "sma-crossover": templateSmaCrossover,
    "rsi-mean-reversion": templateRsiMeanReversion,
    "bollinger-bands": templateBollingerBands,
    "macd-divergence": templateMacdDivergence,
    "trend-following-momentum": templateTrendFollowing,
    "volatility-mean-reversion": templateVolatilityMeanReversion,
    "regime-adaptive": templateRegimeAdaptive,
    "multi-timeframe-confluence": templateMultiTimeframe,
    "risk-parity-triple-screen": templateRiskParity,
    custom: templateCustom,
  };

  const gen = templates[strategyType];
  if (!gen) {
    // Fallback: generic buy-and-hold
    return templateFallback(params);
  }
  return gen(params, def);
}

