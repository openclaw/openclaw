function templateCustom(_p: Record<string, unknown>, def: StrategyDefinition): string {
  // Extract custom rules if available
  const rules = (def as unknown as { _rules?: { buy: string; sell: string } })._rules;
  const buyRule = rules?.buy ?? "rsi < 30";
  const sellRule = rules?.sell ?? "rsi > 70";

  return `${HEADER}
def compute(data):
    """Custom rule-based strategy.
    Buy rule:  ${buyRule}
    Sell rule: ${sellRule}
    """
    df = pd.DataFrame(data)

    # Compute commonly used indicators
    delta = df['close'].diff()
    gain = delta.clip(lower=0).rolling(14).mean()
    loss = (-delta.clip(upper=0)).rolling(14).mean()
    rs = gain / loss.replace(0, np.nan)
    df['rsi'] = 100 - (100 / (1 + rs))
    df['sma'] = df['close'].rolling(20).mean()
    df['ema'] = df['close'].ewm(span=20, adjust=False).mean()
    df['sma_bb'] = df['close'].rolling(20).mean()
    df['std_bb'] = df['close'].rolling(20).std()
    df['bb_upper'] = df['sma_bb'] + 2 * df['std_bb']
    df['bb_lower'] = df['sma_bb'] - 2 * df['std_bb']

    # Evaluate rules
    buy_mask = ${pythonizeRule(buyRule)}
    sell_mask = ${pythonizeRule(sellRule)}

    df['signal'] = 0
    df.loc[buy_mask, 'signal'] = 1
    df.loc[sell_mask, 'signal'] = -1

    df['position'] = df['signal'].replace(0, np.nan).ffill().fillna(0)
    df['returns'] = df['close'].pct_change() * df['position'].shift(1)
    df['equity'] = (1 + df['returns'].fillna(0)).cumprod() * 10000

    trades = []
    pos = 0
    for i, row in df.iterrows():
        if row['signal'] == 1 and pos <= 0:
            trades.append({'date': str(row.get('date', i)), 'action': 'buy', 'price': row['close'], 'amount': 1, 'reason': '${buyRule.replace(/'/g, "\\'")}'})
            pos = 1
        elif row['signal'] == -1 and pos >= 0:
            trades.append({'date': str(row.get('date', i)), 'action': 'sell', 'price': row['close'], 'amount': 1, 'reason': '${sellRule.replace(/'/g, "\\'")}'})
            pos = -1

    return {
        'equity_curve': [{'date': str(row.get('date', i)), 'equity': row['equity']} for i, row in df.dropna(subset=['equity']).iterrows()],
        'trade_journal': trades,
    }
`;
}

/** Convert a TS rule expression to a pandas boolean mask string. */
function pythonizeRule(rule: string): string {
  return (
    rule
      // Map indicator names to dataframe columns
      .replace(/\brsi\b/g, "df['rsi']")
      .replace(/\bsma\b/g, "df['sma']")
      .replace(/\bema\b/g, "df['ema']")
      .replace(/\bclose\b/g, "df['close']")
      .replace(/\bopen\b/g, "df['open']")
      .replace(/\bhigh\b/g, "df['high']")
      .replace(/\blow\b/g, "df['low']")
      .replace(/\bvolume\b/g, "df['volume']")
      .replace(/\bbb\.upper\b/g, "df['bb_upper']")
      .replace(/\bbb\.lower\b/g, "df['bb_lower']")
      // Logical operators
      .replace(/\bAND\b/gi, "&")
      .replace(/\bOR\b/gi, "|")
  );
}

