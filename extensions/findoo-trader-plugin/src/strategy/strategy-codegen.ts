function templateRegimeAdaptive(p: Record<string, unknown>): string {
  const fastEma = p.fastEma ?? 12;
  const slowEma = p.slowEma ?? 26;
  const bbPeriod = p.bbPeriod ?? 20;
  const bbStdDev = p.bbStdDev ?? 2.0;
  const rsiPeriod = p.rsiPeriod ?? 14;
  const rsiOversoldMR = p.rsiOversoldMR ?? 30;
  const rsiOverboughtMR = p.rsiOverboughtMR ?? 70;
  const bandWidthThreshold = p.bandWidthThreshold ?? 0.04;
  const baseAmount = p.baseAmount ?? 100;
  return `${HEADER}${RSI_HELPER}

def compute(data):
    df = pd.DataFrame(data) if not isinstance(data, pd.DataFrame) else data
    closes = df['close']
    if len(closes) < max(${slowEma}, ${bbPeriod}):
        return {"action": "hold", "amount": 0, "reason": "Insufficient data"}

    ema_fast = closes.ewm(span=${fastEma}, adjust=False).mean()
    ema_slow = closes.ewm(span=${slowEma}, adjust=False).mean()
    sma = closes.rolling(${bbPeriod}).mean()
    std = closes.rolling(${bbPeriod}).std()
    upper = sma + ${bbStdDev} * std
    lower = sma - ${bbStdDev} * std
    bw = (upper - lower) / sma

    rsi = _calc_rsi(closes, ${rsiPeriod})
    vol = _calc_volatility(closes)
    regime = _detect_regime(rsi, vol)
    trending = float(bw.iloc[-1]) > ${bandWidthThreshold}

    if trending:
        if ema_fast.iloc[-1] > ema_slow.iloc[-1] and ema_fast.iloc[-2] <= ema_slow.iloc[-2]:
            return {"action": "buy", "amount": ${baseAmount}, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.2, "reason": "EMA cross (trend)"}
        if ema_fast.iloc[-1] < ema_slow.iloc[-1]:
            return {"action": "sell", "amount": ${baseAmount}, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.0, "reason": "EMA bearish (trend)"}
    else:
        if rsi < ${rsiOversoldMR}:
            return {"action": "buy", "amount": ${baseAmount}, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.5, "reason": "RSI oversold (mean-rev)"}
        if rsi > ${rsiOverboughtMR}:
            return {"action": "sell", "amount": ${baseAmount}, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.0, "reason": "RSI overbought (mean-rev)"}

    return {"action": "hold", "amount": 0, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.0, "reason": "No regime signal"}
`;
}

