function templateFallback(_p: Record<string, unknown>): string {
  return `${HEADER}
def compute(data):
    """Fallback: simple buy-and-hold strategy."""
    df = pd.DataFrame(data)
    df['returns'] = df['close'].pct_change()
    df['equity'] = (1 + df['returns'].fillna(0)).cumprod() * 10000

    trades = [{'date': str(df.iloc[0].get('date', 0)), 'action': 'buy', 'price': df.iloc[0]['close'], 'amount': 1, 'reason': 'Buy and hold entry'}]

    return {
        'equity_curve': [{'date': str(row.get('date', i)), 'equity': row['equity']} for i, row in df.dropna(subset=['equity']).iterrows()],
        'trade_journal': trades,
    }
`;
}
