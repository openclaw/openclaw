// Template: SMA Crossover
// ---------------------------------------------------------------------------

function templateSmaCrossover(p: Record<string, unknown>): string {
  const fast = p.fastPeriod ?? 10;
  const slow = p.slowPeriod ?? 30;
  const baseAmount = p.baseAmount ?? 100;
  return `${HEADER}${RSI_HELPER}

def compute(data):
    df = pd.DataFrame(data) if not isinstance(data, pd.DataFrame) else data
    closes = df['close']
    if len(closes) < ${slow}:
        return {"action": "hold", "amount": 0, "reason": "Insufficient data"}

    sma_fast = closes.rolling(${fast}).mean()
    sma_slow = closes.rolling(${slow}).mean()

    rsi = _calc_rsi(closes)
    vol = _calc_volatility(closes)
    regime = _detect_regime(rsi, vol)

    # Golden cross: fast crosses above slow
    if sma_fast.iloc[-1] > sma_slow.iloc[-1] and sma_fast.iloc[-2] <= sma_slow.iloc[-2]:
        return {"action": "buy", "amount": ${baseAmount}, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.0, "reason": "SMA golden cross"}
    # Death cross: fast crosses below slow
    if sma_fast.iloc[-1] < sma_slow.iloc[-1] and sma_fast.iloc[-2] >= sma_slow.iloc[-2]:
        return {"action": "sell", "amount": ${baseAmount}, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.0, "reason": "SMA death cross"}

    return {"action": "hold", "amount": 0, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.0, "reason": "No crossover"}
`;
}

