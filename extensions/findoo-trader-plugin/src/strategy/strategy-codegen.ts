// Template: MACD Divergence
// ---------------------------------------------------------------------------

function templateMacdDivergence(p: Record<string, unknown>): string {
  const fast = p.fastPeriod ?? 12;
  const slow = p.slowPeriod ?? 26;
  const signal = p.signalPeriod ?? 9;
  const baseAmount = p.baseAmount ?? 100;
  return `${HEADER}${RSI_HELPER}

def compute(data):
    df = pd.DataFrame(data) if not isinstance(data, pd.DataFrame) else data
    closes = df['close']
    if len(closes) < ${slow} + ${signal}:
        return {"action": "hold", "amount": 0, "reason": "Insufficient data"}

    ema_fast = closes.ewm(span=${fast}, adjust=False).mean()
    ema_slow = closes.ewm(span=${slow}, adjust=False).mean()
    macd = ema_fast - ema_slow
    macd_signal = macd.ewm(span=${signal}, adjust=False).mean()
    hist = macd - macd_signal

    rsi = _calc_rsi(closes)
    vol = _calc_volatility(closes)
    regime = _detect_regime(rsi, vol)

    if float(hist.iloc[-1]) > 0 and float(hist.iloc[-2]) <= 0:
        return {"action": "buy", "amount": ${baseAmount}, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.0, "reason": "MACD histogram cross up"}
    if float(hist.iloc[-1]) < 0 and float(hist.iloc[-2]) >= 0:
        return {"action": "sell", "amount": ${baseAmount}, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.0, "reason": "MACD histogram cross down"}

    return {"action": "hold", "amount": 0, "regime": regime, "rsi": rsi, "volatility": vol, "multiplier": 1.0, "reason": "No MACD crossover"}
`;
}

