function templateSmaCrossover(p: Record<string, unknown>): string {
  const fast = p.fastPeriod ?? 10;
  const slow = p.slowPeriod ?? 30;
  return `${HEADER}
def compute(data):
    df = pd.DataFrame(data)
    df['sma_fast'] = df['close'].rolling(${fast}).mean()
    df['sma_slow'] = df['close'].rolling(${slow}).mean()

    df['signal'] = 0
    df.loc[(df['sma_fast'] > df['sma_slow']) & (df['sma_fast'].shift(1) <= df['sma_slow'].shift(1)), 'signal'] = 1
    df.loc[(df['sma_fast'] < df['sma_slow']) & (df['sma_fast'].shift(1) >= df['sma_slow'].shift(1)), 'signal'] = -1

    df['position'] = df['signal'].replace(0, np.nan).ffill().fillna(0)
    df['returns'] = df['close'].pct_change() * df['position'].shift(1)
    df['equity'] = (1 + df['returns'].fillna(0)).cumprod() * 10000

    trades = []
    pos = 0
    for i, row in df.iterrows():
        if row['signal'] == 1 and pos <= 0:
            trades.append({'date': str(row.get('date', i)), 'action': 'buy', 'price': row['close'], 'amount': 1, 'reason': 'SMA golden cross'})
            pos = 1
        elif row['signal'] == -1 and pos >= 0:
            trades.append({'date': str(row.get('date', i)), 'action': 'sell', 'price': row['close'], 'amount': 1, 'reason': 'SMA death cross'})
            pos = -1

    return {
        'equity_curve': [{'date': str(row.get('date', i)), 'equity': row['equity']} for i, row in df.dropna(subset=['equity']).iterrows()],
        'trade_journal': trades,
    }
`;
}

