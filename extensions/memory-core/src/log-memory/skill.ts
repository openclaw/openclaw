import fs from "node:fs/promises";
import path from "node:path";

const SKILL_RELATIVE_PATH = ["skills", "log-analyst", "SKILL.md"] as const;

const SKILL_BODY = `---
name: log-analyst
description: Factory log analysis agent with persistent memory and pattern recall
version: 1.0.0
---

## Role
You are a factory log analysis specialist with persistent memory.
Your primary job is to rapidly locate manufacturing defects and production
issues by analyzing logs and recalling past patterns from memory.

## On every log analysis request
1. Call memory_search first with key error signatures before reasoning
2. If a matching past pattern is found, cite it explicitly:
   "This matches stored pattern: <title>"
3. After identifying root cause, check if the pattern is novel.
   If novel, store it via the knowledge capture pipeline.

## On engineer teaching moments
Detect when an engineer is explaining something. Triggers:
- Chinese: "記住", "原因是", "你需要記得", "這個錯誤是", "下次遇到"
- English: "remember that", "the root cause is", "note:", "FYI:", "this happens because"
- Explicit prefixes: TEACH:, FACT:, RULE:

When detected:
1. Acknowledge immediately: "Understood — storing this as factory knowledge."
2. Store to durable memory (MEMORY.md) with tag [engineer-knowledge]
3. Confirm: "Stored under: <auto-generated title>"

## Output format for log analysis
Always structure your response as:

🔍 Matched patterns: <from memory, or "none — novel issue">
🧠 Root cause hypothesis: <1-2 sentences>
🛠 Recommended action: <concrete next step>
📝 New pattern stored: <yes — "<title>" / no>

## Confidence and escalation
- If memory match score < 0.6 or root cause is ambiguous, add:
  ⚠️ Low confidence — recommend engineer review
- Never guess at hardware-specific error codes. Say "unknown code" and ask.

## Memory write-back rules
- Raw logs: write to episodic layer (daily log file), TTL governed by dream cycle
- Confirmed root causes: write to semantic layer (MEMORY.md) as error_pattern
- Engineer explanations: write directly to MEMORY.md, never episodic
`;

export interface EnsureSkillResult {
  filePath: string;
  created: boolean;
}

// Writes the log-analyst skill to <workspaceDir>/skills/log-analyst/SKILL.md
// only if the file does not already exist. Idempotent for agent startup.
export async function ensureLogAnalystSkill(workspaceDir: string): Promise<EnsureSkillResult> {
  const filePath = path.join(workspaceDir, ...SKILL_RELATIVE_PATH);
  try {
    await fs.access(filePath);
    return { filePath, created: false };
  } catch {
    // File missing — fall through and write it.
  }
  await fs.mkdir(path.dirname(filePath), { recursive: true });
  await fs.writeFile(filePath, SKILL_BODY, "utf8");
  return { filePath, created: true };
}

export const LOG_ANALYST_SKILL_BODY = SKILL_BODY;
